<p><lt1><tp>lon</tp></lt1>
<p><tp>lon</tp> can be used as both a verb and a preposition (and so can <tp>tawa</tp> and <tp>kepeken</tp>, as you'll learn below). Study these examples:<br>
<ex><tp>mi lon tomo.</tp> <eq></eq> <eng>I'm in the house.</eng><br>
<ex><tp>mi moku lon tomo.</tp> <eq></eq> <eng>I eat in the house.</eng><br>
In the first sentence, <tp>lon</tp> is a verb. In the second, it's a preposition. Neat, huh? More examples:<br>
<ex><tp>suno li lon sewi.</tp> <eq></eq> <eng>The sun is in the sky.</eng><br>
<ex><tp>mi telo e mi lon tomo telo.</tp> <eq></eq> <eng>I bathe myself in the restroom.</eng><br>
<xp>Remember from lesson 5 that <tp>tomo telo</tp> means restroom, bathroom, etc.</xp><br>
<ex><tp>kili li lon poki.</tp> <eq></eq> <eng>The fruit is in the basket.</eng><br>

<p><lt2><tp>lon</tp> as an action verb</lt2>

<p>Although <tp>lon</tp> is a very common Toki Pona word, it's almost never used as an action verb. I'm including this section anyway for those who are curious, but feel free to skip over it.

<p>... So you're still reading? Good! If used as an action verb, <tp>lon</tp> basically means <eng>to make ___ real</eng> or <eng>to make ___ aware/awake/conscious.</eng> Let's look at a few examples:<br>
<ex><tp>sina lon e wile sina.</tp> &nbsp;&nbsp; This would be similar to saying, "You're following your dreams," in English.<br>
<ex><tp>sina wile lon e ona!</tp> &nbsp;&nbsp; This would be similar to saying, "You have to snap her out of it," in English. You might use <tp>lon</tp> this way if someone were despondent, not paying attention, had dozed off, etc.

<p><lt1><tp>kepeken</tp></lt1>

<p>It's easy to use <tp>kepeken</tp> as an action verb. You use <tp>e</tp> after it like with most other verbs.<br>
<ex><tp>mi kepeken e ilo.</tp> <eq></eq> <eng>I'm using tools.</eng><br>
<ex><tp>sina wile kepeken e ilo.</tp> <eq></eq> <eng>You have to use tools.</eng><br>
<ex><tp>mi kepeken e poki ni.</tp> <eq></eq> <eng>I'm using that cup.</eng>

<p><tp>kepeken</tp> can also be a preposition.<br>
<ex><tp>mi moku kepeken ilo moku.</tp> <eq></eq> <eng>I eat using a fork.</eng><br>
<ex><tp>mi lukin kepeken ilo suno.</tp> <eq></eq> <eng>I look using a flashlight.</eng> (<tp>ilo suno</tp> = a tool of light, hence flashlight)<br>

<p><lt1><tp>tawa</tp></lt1>

<p><lt2><tp>tawa</tp> as a verb</lt2>

<p>Hopefully you understood how to use <tp>lon</tp> because <tp>tawa</tp> is used in a similar way. As with <tp>lon</tp>, don't put an <tp>e</tp> after <tp>tawa</tp>.<br>
<ex><tp>mi tawa tomo mi.</tp> <eq></eq> <eng>I'm going to my house.</eng><br>
<ex><tp>ona li tawa utala.</tp> <eq></eq> <eng>He's going to the war.</eng><br>
<ex><tp>sina wile tawa telo suli.</tp> <eq></eq> <eng>You want to go to the ocean.</eng><br>
<ex><tp>ona li tawa sewi kiwen.</tp> <eq></eq> <eng>She's going up the rock.</eng><br>
<xp>In the last example, <tp>sewi</tp> is a noun. A more literal translation of that sentence is, <eng>She's going to the top (of) the rock.</eng></xp>

<p>You can't put an <tp>e</tp> after <tp>lon</tp> and <tp>tawa</tp> because there's no direct object. For example, if you go home, you're not actually <b>doing</b> anything to your house. You're simply going to it. If you tried to burn your house down or <b>do</b> something else to it or 
affect it in some way, then you'd put <tp>e</tp> after the verb. Otherwise, no <tp>e</tp>.

<p><lt2><tp>tawa</tp> as an *action* verb</lt2>

<p>However, if you're <b>moving</b> an object, then use <tp>tawa e</tp>.<br>
<ex><tp>mi tawa e kiwen.</tp> <eq></eq> <eng>I moved the rock.</eng><br>
<ex><tp>ona li tawa e len mi.</tp> <eq></eq> <eng>She moved my clothes.</eng>

<p><lt2><tp>tawa</tp> as a preposition</lt2>

<p>In addition to being a verb, <tp>tawa</tp> is also a preposition.<br>
<ex><tp>mi toki tawa sina.</tp> <eq></eq> <eng>I talk to you.</eng><br>
<ex><tp>ona li lawa e jan tawa ma pona.</tp> <eq></eq> <eng>He led people to the good land.</eng><br>
<ex><tp>ona li kama tawa ma mi.</tp> <eq></eq> <eng>He's coming to my country.</eng>

<p>This is a good time to learn how to say that you like something, since the Toki Pona phrase for <eng>to like</eng> requires you to use <tp>tawa</tp> as a preposition:<br>
<ex><tp>ni li pona tawa mi.</tp> <eq></eq> <eng>That is good to me,</eng> i.e. I like that.<br>
To say that you dislike something, change <tp>pona</tp> to <tp>ike</tp>.<br>
<ex><tp>ni li ike tawa mi.</tp> <eq></eq> <eng>That is bad to me,</eng> i.e. I don't like that.<br>
More examples:<br>
<ex><tp>kili li pona tawa mi.</tp> <eq></eq> <eng>I like fruit.</eng><br>
<ex><tp>toki li pona tawa mi.</tp> <eq></eq> <eng>I like talking,</eng> or <eng>I like languages.</eng><br>
<ex><tp>tomo li ike tawa mi.</tp> <eq></eq> <eng>I don't like buildings.</eng><br>
<ex><tp>telo suli li ike tawa mi.</tp> <eq></eq> <eng>I don't like the ocean.</eng>

<p>Remember that you can only use <tp>e</tp> after a verb. Toki Pona does not use clauses. For example, to say, "I like watching the countryside," you could <b>not</b> say "<tp>lukin e ma li pona tawa mi.</tp>" Instead you should split the sentence in two:<br>
<ex><tp>mi lukin e ma. ni li pona tawa mi.</tp> <eq></eq> <eng>I'm watching the countryside. This is good to me.</eng><br>
Alternatively, you could express the same idea using what you learned in <a href="okamasona5.php">lesson 5</a>:<br>
<ex><tp>ma li pona lukin.</tp> <eq></eq> <eng>The countryside is good visually</eng> i.e. the countryside is good-looking, pretty, etc.

<p><lt2><tp>tawa</tp> as the preposition <eng>for</eng></lt2>

<p><tp>tawa</tp> can also mean <tp>for</tp>, as in this sentence:<br>
<ex><tp>mi pona e tomo <u>tawa</u> jan pakala.</tp> <eq></eq> <eng>I fixed the house <u>for</u> the disabled man.</eng><br>
Unfortunately, using <tp>tawa</tp> to mean both <eng>to</eng> and <eng>for</eng> has some drawbacks. Keep reading to see how.

<p><lt2><tp>tawa</tp> as an adjective</lt2>
<p>You know how some words in Toki Pona can be used as nouns, verbs, or adjectives, depending on context? Well, <tp>tawa</tp> is the same way. <tp>tawa</tp> is used as an adjective to make the phrase we use for <eng>car:</eng><br>
<ex><tp>tomo tawa</tp> <eq></eq> <eng>car</eng> (literally, <eng>moving structure</eng>)<br>
<ex><tp>tomo tawa telo</tp> <eq></eq> <eng>boat</eng> or <eng>ship</eng><br>
<ex><tp>tomo tawa kon</tp> <eq></eq> <eng>airplane,</eng> <eng>helicopter,</eng> etc.

<p><lt2>Ambiguity of <tp>tawa</tp></lt2>
<p>Read the next sentence, then stop to think about it for few moments. Try to think of different meanings it might have.<br>
<ex><tp>mi pana e tomo tawa sina.</tp><br>
If <tp>tawa</tp> is an adjective, then this sentence translates as, <eng>I gave your car.</eng> But if <tp>tawa</tp> is a preposition, it's, <eng>I gave the house to you.</eng> So how do you tell the difference? You don't! This is one of those problems inherent in Toki Pona that there's no way to avoid. Fortunately, such problems are rare.

<p><lt1><tp>e ni:</tp></lt1>

<p>Toki Pona doesn't use relative clauses. (If you don't know what a relative clause is: In the sentence, "I know that he likes me," the phrase "that he likes me" is a relative clause.)

<p>Toki Pona instead splits such sentences in two using <tp>e ni:</tp>. Here's an example:
<ex><tp>sina toki e ni tawa mi: sina moku.</tp> <eq></eq> <eng>You told me that you are eating.</eng>

<p><lt1><tp>kama</tp></lt1>

<p>We've already learned a little about <tp>kama</tp>, but it's a common and versatile word, so we need to study it more closely.

<p>As you've already seen, <tp>kama</tp> can be used with <tp>tawa</tp>, like this:<br>
<ex><tp>ona li kama tawa tomo mi.</tp> <eq></eq> <eng>He came to my house.</eng>

<p><tp>kama</tp> can also be an action verb. It means <eng>to cause</eng> or <eng>to bring about</eng>:<br>
<ex><tp>mi kama e pakala.</tp> <eq></eq> <eng>I caused an accident.</eng><br>
<ex><tp>sina kama e ni: mi wile moku.</tp> <eq></eq> <eng>You caused this: I want to eat,</eng> i.e. you made me hungry.</eng><br>
<xp>Note that you can't say, "<tp>sina kama e mi wile moku</tp>." <b>Toki Pona does not use clauses</b>; you can't shove pieces of sentence together. If in doubt, it's usually wise to split a sentence into two.</xp><p>

<p><tp>kama</tp> can also be used with infinitives to make a progressive-like effect. For now we'll just learn <tp>kama jo</tp>, which means <eng>get.</eng><br>
<ex><tp>mi kama jo e telo.</tp> <eq></eq> <eng>I came to have the water,</eng> i.e. I got the water.<br>

