<p><lt1>Negation</lt1>

<p>In English you make a verb negative by adding <tp>doesn't</tp> or <tp>don't</tp> in front of the it, like in the sentence, <eng>We <b>don't</b> talk.</eng> However, Toki Pona's negative word, <tp>ala</tp>, goes <b>after</b> the verb.<br>
<ex><tp>mi lape ala.</tp> <eq></eq> <eng>I'm not sleeping.</eng> (literally: <eng>I sleep not.</eng>)<br>
<ex><tp>mi musi ala.</tp> <eq></eq> <eng>I'm not having fun.</eng><br>
<ex><tp>mi wawa ala.</tp> <eq></eq> <eng>I'm not strong.</eng><br>
<ex><tp>mi wile ala tawa musi.</tp> <eq></eq> <eng>I don't want to dance.</eng><br>
<xp><tp>tawa musi</tp> literally means <eng>move entertainingly</eng> or something like that. It means <eng>dance.</eng>

<p>Oftentimes <tp>ala</tp> sentences can be translated in various ways. For example, the sentence "<tp>mi musi ala</tp>" could also be translated as <eng>I'm bored.</eng> <eng>mi wawa ala</eng> could be translated as <eng>I'm weak.</eng> Be flexible.

<p><tp>ala</tp> can also be an adjective.<br>
<ex><tp>jan ala li toki.</tp> <eq></eq> <eng>Nobody is talking.</eng> (literally, <eng>person none (li) talks.</eng>)<br>
However, you shouldn't use <tp>ala</tp> with <tp>ijo</tp>.<br>
<ex>Wrong: <tp>ijo ala li jaki.</tp> <eq></eq> <eng>No thing is dirty.</eng> (literally, "thing none <tp>li</tp> dirty.")<br>
<ex>Right: <tp>ala li jaki.</tp> <eq></eq> <eng>Nothing is dirty.</eng><br>
By itself, <tp>ala</tp> already means <eng>nothing</eng> by default, so <tp>ijo ali</tp> is needlessly complex.

<p><lt1><tp>ale</tp> and <tp>ali</tp></lt1>

<p>It may seem odd that I'm teaching <tp>ali</tp> (<eng>everything</eng>) in the lesson about negation, but despite the different meanings, <tp>ala</tp> and <tp>ali</tp> are used in similar ways.

<p>First, though, let's discuss why there are two words (<tp>ale</tp> and <tp>ali</tp>) that mean the same thing. Originally, Toki Pona only had the word <tp>ale</tp>. However, because <tp>ale</tp> sounds very similar to <tp>ala</tp>, Toki Ponans were worried that listeners would hear the word wrong. Therefore, Sonja added <tp>ali</tp> as an alternative pronunciation. You can choose whichever form you prefer, but I prefer <tp>ali</tp>, and that's what I'll use throughout the rest of this course.

<p>Above you learned that <tp>ala</tp> can be used as an adjective. Fortunately, <tp>ali</tp> is used the exact same way:<br>
<ex><tp>jan ali li wile tawa.</tp> <eq></eq> <eng>Everybody wants to travel.</eng><br>
<ex><tp>ma ali li pona.</tp> <eq></eq> <eng>All nations are good.</eng><br>
Also like <tp>ala</tp>, it's best not to use <tp>ijo</tp> and <tp>ali</tp> together:<br>
<ex><tp><s>ijo</s> ali li pona</tp> <eq></eq> <eng>Everything is okay.</eng><br>

<p><lt1>Yes/No Questions</lt1>

<p>To ask a question that can be answered by saying "yes" or "no," you follow a simple pattern. Look at how this sentence has been made into a question:<br>
<ex><tp>sina pona ala pona?</tp> <eq></eq> <eng>Are you okay?</eng> (literally, <eng>you okay not okay?</eng>)<br>
To ask yes/no questions, say the verb, then <tp>ala</tp>, then repeat the verb. Here are more examples:<br>
<ex><tp>suno li suli ala suli?</tp> <eq></eq> <eng>Is the sun big?</eng><br>
<ex><tp>len sina li telo ala telo?</tp> <eq></eq> <eng>Are your clothes wet?</eng><br>
<ex><tp>tomo tawa sina li pakala ala pakala?</tp> <eq></eq> <eng>Is your car messed up?</eng><br>
<ex><tp>sina ken ala ken lape?</tp> <eq></eq> <eng>Can you sleep?</eng><br>
<ex><tp>ona li lon ala lon tomo?</tp> <eq></eq> <eng>Is he in the house?</eng><br>
<ex><tp>ona li tawa ala tawa ma ike?</tp> <eq></eq> <eng>Did he go to the evil land?</eng><br>
<ex><tp>sina pana ala pana e moku tawa jan lili?</tp> <eq></eq> <eng>Did you give food to the child?</eng><br>
<ex><tp>pipi li moku ala moku e kili?</tp> <eq></eq> <eng>Are the bugs eating the fruit?</eng>

<p><lt2>Answering Yes/No Questions</lt2>

<p>To answer a yes/no question, just repeat the verb if you mean "yes." To say "no," just repeat the verb and add <tp>ala</tp> after it. For example, if someone asked you, "<tp>sina wile ala wile moku?</tp>" (<eng>Do you want to eat?</eng>),<br>
<ex><tp>wile</tp> <eq></eq> <eng>Yes</eng><br>
<ex><tp>wile ala</tp> <eq></eq> <eng>No</eng>

<p>Here are some more questions and their answers:<br>
<ex><tp>sina lukin ala lukin e kiwen?</tp> <eq></eq> <eng>Do you see the rock?</eng><br>
<ex><tp>&nbsp;&nbsp;lukin</tp> <eq></eq> <eng>Yes</eng><br>
<ex><tp>&nbsp;&nbsp;lukin ala</tp> <eq></eq> <eng>No</eng><br>
<ex><tp>sina sona ala sona e toki mi?</tp> <eq></eq> <eng>Do you understand my talking? Do you understand what I'm saying?</eng><br>
<ex><tp>&nbsp;&nbsp;sona</tp> <eq></eq> <eng>Yes</eng><br>
<ex><tp>&nbsp;&nbsp;sona ala</tp> <eq></eq> <eng>No</eng>

<p><lt1><tp>tan</tp> (again)</lt1>

<p>In <a href="okamasona7.php">lesson 7</a> you learned that <tp>tan</tp> is a preposition, but now you need to learn how to use it as a noun.

<p>As a noun, <tp>tan</tp> means <eng>cause</eng> or <eng>reason.</eng> You can use <tp>tan</tp> to translated a sentence like, "I don't know why," but you'll have to rephrase that sentence a little first:<br>
<ex><tp>mi sona ala e tan.</tp> <eq></eq> <eng>I don't know the reason,</eng> i.e. I don't know why.

