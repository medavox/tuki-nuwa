<p><lt1>Color Combinations</lt1>

<p>Before you begin this lesson, look at the vocabulary above. Notice that there are words for a few colors, but no words for purple, green, orange, grey, etc. That's because these words don't exist in Toki Pona. With that in mind, look at this picture:<p>
<center><img src="learn/oks/13purple.gif"></center><p>
Although the image looks purple, it's actually a grid full of red and blue squares. Here's a close-up of part of that same picture:<p>
<center><img src="learn/oks/13redandblue.gif"></center>
<p>

As you know, combining red and blue makes purple. Toki Pona uses this color-combination concept to describe other colors. Here's an example:<br>
<ex><tp>len</tp> <eq></eq> <eng>clothing</eng><br>
<ex><tp>len laso</tp> <eq></eq> <eng>blue clothing</eng><br>
<ex><tp>len laso loje</tp> <eq></eq> <eng>purple clothing</eng> (literally: <eng>reddish blue clothing</eng>)

<p>The color-combination concept lets you describe many different colors using just a few words. Here's a list of some of the common ones:<br>
<ex><tp>laso loje</tp> <eq></eq> purple (<eng>reddish blue</eng>)<br>
<ex><tp>laso jelo</tp> <eq></eq> green (<eng>yellowish blue</eng>)<br>
<ex><tp>loje jelo</tp> <eq></eq> orange (<eng>yellowish red</eng>)<br>
<ex><tp>loje walo</tp> <eq></eq> pink (<eng>whitish red</eng>)<br>
<ex><tp>walo pimeja</tp> <eq></eq> grey (<eng>dark white</eng>)<br>
You can string as many color words together as you want, but try not go get carried away. The goal of Toki Pona is simplicity, after all, so keep it basic.

<p>There's not much logic to govern the order in which you should list the colors, so you have some flexibility.<br>
<ex><tp>laso loje</tp> <eq></eq> <eng>reddish blue</eng> = purple<br>
<ex><tp>loje laso</tp> <eq></eq> <eng>bluish red</eng> = purple<br>
<ex><tp>laso jelo</tp> <eq></eq> <eng>yellowish blue</eng> = green<br>
<ex><tp>jelo laso</tp> <eq></eq> <eng>bluish yellow</eng> = green

<p><lt2><tp>pi</tp> and Colors</lt2>

<p>Suppose that you have a shirt that looks like this:<br>
<center><img src="learn/oks/13shirt.jpg"></center><br>
Of course, the shirt is both <tp>loje</tp> (<eng>red</eng>) and <tp>laso</tp> (<eng>blue</eng>), but you can't simply call it <tp>len loje laso</tp> because that would mean <eng>purple shirt</eng> as you learned above.

<p>To resolve this problem, use <tp>en</tp> to separate the two colors, and use <tp>pi</tp> to show that the two colors both modify <tp>len</tp> separately:<br>
<ex><tp>len pi loje en laso</tp> <eq></eq> shirt of red and blue

<p>Make sense? Just to emphasize the importance of <tp>pi</tp>, let's see what would happen if you accidentally omitted it:<br>
<ex><tp>len loje en laso li pona.</tp> <eq></eq> <eng>A red shirt and blueness are good.</eng><br>
Without <tp>pi</tp>, <tp>laso</tp> has nothing to modify and actually becomes a noun, so the sentence makes no sense. Use <tp>pi</tp> to show that both <tp>loje</tp> and <tp>laso</tp> modify <tp>len</tp> <b>separately</b>.

<p><lt1><tp>kule</tp></lt1>

<p><tp>kule</tp> has two main uses. Let's look at them one at a time.<p>

<lt2>Using <tp>kule</tp> to ask what color something is</lt2>

<p>If you understand <tp>seme</tp>, asking what color something is should be self-explanatory, but here's an example:<br>
<ex><tp>ni li kule seme?</tp> <eq></eq> What color is that? (literally: <eng>That is color which?</eng>)

<p><lt2><tp>kule</tp> as a verb</lt2>

<p><tp>kule</tp> as a verb means <eng>to color</eng> or <eng>to paint.</eng> Here's an example:<br>
<ex><tp>mi kule e lipu.</tp> <eq></eq> <eng>I'm coloring the paper.</eng>

<p><lt1>Miscellaneous</lt1>

<p><tp>sitelen</tp> is the word for this lesson's miscellaneous section. As a noun it means <eng>picture</eng> or <eng>image,</eng> and it's frequently used to make compound nouns, several of which are described below. As a verb <tp>sitelen</tp> means <eng>to draw</eng> or <eng>to write.</eng>

<p><tp>sitelen tawa</tp> (literally, <eng>moving picture</eng>) means a movie or a TV show. Some examples:<br>
<ex><tp>sitelen tawa</tp> The Simpsons <tp>li pona tawa mi.</tp> <eq></eq> <eng>I like the TV show <i>The Simpsons</i>.</eng><br>
<ex><tp>sitelen tawa</tp> The Godfather <tp>li pona kin.</tp> <eq></eq> <eng>The movie <i>The Godfather</i> is good, too.</eng><br>
<tp>sitelen</tp> can also be used in other combinations. <tp>sitelen ma</tp> means <eng>map,</eng> for example.

