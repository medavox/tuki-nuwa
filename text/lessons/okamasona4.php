<p><small>* The <a href="dictionary.php">official dictionary</a> lists <tp>lukin</tp> and <tp>oko</tp>--a word you'll learn in <a href="okamasona15.php">lesson 15</a>--as being interchangeable. Traditionally, though, these two words had different connotations and uses, and most Toki Ponans seem to use these two words according to their traditional definitions, so this course will teach them that way.</small>

<p><lt1>Direct objects using <tp>e</tp></lt1>

<p>In the last lesson, you learned that phrases like <tp>mi moku</tp> could have two potential meanings: <eng>I'm eating</eng> or <eng>I am food.</eng> You must heavily depend on context when reading such sentences. However, there is a way to distinguish between <eng>I'm eating</eng> versus <eng>I am food.</eng> Look at how <tp>e</tp> is used in these two examples:<br>
<ex><tp>mi moku e kili.</tp> <eq></eq> <eng>I eat fruit.</eng><br>
<ex><tp>ona li lukin e pipi.</tp> <eq></eq> <eng>He's watching the bug.</eng><br>
Whatever is getting action done on itself is the "direct object," and in Toki Pona, we use <tp>e</tp> to separate the verb and its direct object.

<p>In the last lesson, you also learned that <tp>sina pona</tp>, like <tp>mi moku</tp>, has two possible meanings: <eng>You are good</eng> or <eng>You're fixing.</eng> Realistically, it would mean <eng>You are good</eng> simply because no one really says <eng>I'm fixing</eng> without specifying what they're fixing. With <tp>e</tp>, you can now specify:<br>
<ex><tp>ona li pona e ilo.</tp> <eq></eq> <eng>She's fixing the machine.</eng><br>
<ex><tp>mi pona e ijo.</tp> <eq></eq> <eng>I'm fixing something.</eng><br>
Many other languages (e.g., Latin, German, and Esperanto) use special features to denote the direct object, so you may already be familiar with this concept, but if not, 
practice using it. (Don't forget to practice <tp>li</tp>, too). As you progress into Toki Pona, you'll see why <tp>e</tp> is necessary.

<p><lt1>Direct objects using <tp>e</tp> with <tp>wile</tp></lt1>

<p>If you want to do something, you can say so like this:<br>
<ex><tp>mi wile lukin e ma.</tp> <eq></eq> <eng>I want to see the countryside.</eng><br>
<ex><tp>mi wile pakala e sina.</tp> <eq></eq> <eng>I must destroy you.</eng><br>
As these examples show, <tp>e</tp> comes after the infinitive in these two sentences, not before it. A few people have mistakenly said things such as, "mi 
wile <b>e</b> pakala e sina," so be careful not to make the same mistake. 

<p><lt1>Compound sentences</lt1>

<p>There are two ways to make compound sentences in Toki Pona: One involves <tp>li</tp>, and the other involves <tp>e</tp>. Since you've now studied both of these words, we'll cover how to use both of them to make compound sentences.

<p><lt2>Compound Sentences with <tp>li</tp></lt2>

<p>Look at this example:<br>
<ex><tp>pipi li lukin li unpa.</tp> <eq></eq> <eng>The bug looks and has sex.</eng><br>
If you put <tp>li</tp> before each verb, you can say that the subject (<tp>pipi</tp> in this sentence) does more than one thing.

<p>This multiple-<tp>li</tp> is slightly different if the sentence begins with <tp>mi</tp> or <tp>sina</tp>:<br>
<ex><tp>mi moku li pakala.</tp> <eq></eq> <eng>I eat and destroy.</eng><br>
Because this sentence starts with <tp>mi</tp>, you must omit <tp>li</tp> before the first verb (<tp>moku</tp>) (Review <a href="okamasona3.php">lesson 3</a> if 
you've forgotten this rule.), but still use it before the second verb (<tp>pakala</tp>). Without the <tp>li</tp> there, the sentence would be chaotic and confusing.

<p><lt2>Compound Sentences with <tp>e</tp></lt2>

<p>If there are several direct objects of the same verb, use multiple <tp>e</tp>'s like in this example:<br>
<ex><tp>mi moku e kili e telo.</tp> <eq></eq> <eng>I eat/drink fruit and water.</eng><br>
You use <tp>e</tp> multiple times because <tp>kili</tp> and <tp>telo</tp> are both direct objects, and <tp>e</tp> must precede them 
both. Here's another example:<br>
<ex><tp>mi wile lukin e ma e suno.</tp> <eq></eq> <eng>I want to see the land and the sun.</eng>

