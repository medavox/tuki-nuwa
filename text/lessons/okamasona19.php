<p><lt1>pini a!</lt1>

<p>sina sona pona e toki pona. toki pona li pona ala pona tawa sina? mi wile e ni: ona li pona tawa sina.

<p>sina wile pali e seme kepeken sona sin sina?

<p>o kama tawa <a href="https://www.facebook.com/groups/sitelen/?fref=ts">kulupu Facebook pi toki pona</a>. o lukin e <a href="http://www.toki.co.nr">lipu ante mi</a> anu <a href="http://www.tokipona.info">lipu pi jan ante</a>. o pali e lipu sin! o pana e sona pi toki pona tawa jan ante.

<p>tenpo kama la sina ken tawa <a href="http://www.tokipona.info/Renkonti%C4%9Doj">kulupu pona pi toki pona</a>.

<p>sina kama tawa ma <a href="https://en.wikipedia.org/wiki/Georgia_%28U.S._state%29">Georgia</a> lon ma Mewika la <a href="contact.php">o pana e lipu tawa mi</a> tan ni: mi wile lukin e sina! mi lon ma tomo <a href="https://en.wikipedia.org/wiki/Macon,_Georgia">Macon</a>.

