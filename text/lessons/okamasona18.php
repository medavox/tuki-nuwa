<p><lt1>New Words</lt1>

<p>Since this <i>o kama sona e toki pona!</i> course was first published circa 2003, Toki Pona has undergone several changes, including the addition of several new vocabulary words. I've opted to segregate those new words into this one lesson. Below I will teach each new word one at a time.

<p>Please be aware that I will <i>only</i> be covering new words that appear in Sonja Lang's official Toki Pona book, <a href="http://www.amazon.com/Toki-Pona-The-Language-Good/dp/0978292308"><i>Toki Pona: The Language of Good</i></a>, which was published in 2014. Although a few other words were proposed (and are still being actively used by some Toki Ponans), I will not name or teach those words here.

<p><lt1><tp>alasa</tp></lt1>

<p><tp>alasa</tp> describes hunting or foraging for food and other resources. Two examples:<br>
<ex><tp>tenpo pini la jan ali li alasa e soweli e kili.</tp> <eq></eq> <eng>In the past, everybody hunted/gathered buffalo and fruit.</eng><br>
<ex><tp>jan alasa pona li wawa li sona pona e ma ona li pona tawa meli mute.</tp> <eq></eq> <eng>A good hunter is strong, understands his land well, and is liked by many women.</eng>

<p><lt1><tp>esun</tp></lt1>

<p><tp>esun</tp> is a marketplace.<br>
<ex><tp>mi pali lon esun suli.</tp> <eq></eq> <eng>I work at a supermarket.</eng><br>
<ex><tp>jan ike utala pi nasin sewi Silami li moli e jan mute lon esun Kenja.</tp> <eq></eq> <eng>Muslim terrorists murdered a lot of people in a Kenyan mall.</eng>

<p><lt1><tp>namako</tp></lt1>

<p>When <tp>namako</tp> was first proposed as a new word, it was defined as:<br>
<ex><i>n</i> food additive, accessory, something extra<br>
<ex><i>vt</i> season, embellish, stimulate

<p>By the time Sonja published <a href="http://www.amazon.com/Toki-Pona-The-Language-Good/dp/0978292308">the official book</a>, however, she had changed <tp>namako</tp> to be merely an alternate form of the word <tp>sin</tp>, which (as you learned in <a href="okamasona10.php">lesson 10</a>) means <eng>new,</eng> <eng>another,</eng> <eng>fresh,</eng> etc.

<p>In actual use, Toki Ponans tend to use <tp>namako</tp> according to the original definition, not as a mere alternate of <tp>sin</tp>.

<p><lt1><tp>pan</tp></lt1>

<p><tp>pan</tp> seems to describe many of the edible plants from the <a href="https://en.wikipedia.org/wiki/Poaceae">Poaceae family</a> or foods based on those plants. 
<p>The <a href="http://www.amazon.com/Toki-Pona-The-Language-Good/dp/0978292308">official book</a> defines <tp>pan</tp> as "cereal, grain; barley, corn, oat, rice, wheat; bread, pasta."

<p><lt1><tp>pu</tp></lt1>

<p><tp>pu</tp> basically means <eng>to read the <a href="http://www.amazon.com/Toki-Pona-The-Language-Good/dp/0978292308">official Toki Pona book</a>.</eng><br>
<ex><tp>mi wile pu lon tomo telo.</tp> <eq></eq> <eng>I want to read the official Toki Pona book in the bathroom.</eng>

