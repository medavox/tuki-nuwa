<p><lt1><tp>la</tp></lt1>

<p>Along with <tp>pi</tp>, <tp>la</tp> is one of Toki Pona's more misunderstood words.

<p>Since <tp>la</tp> is basically used in three different ways, we'll study them one at at time.

<p><lt2><tp>ken la ...</tp></lt2>

<p>To say <eng>maybe</eng> in Toki Pona, add <tp>ken la</tp> before the sentence in question.<br>
<ex><tp>ilo li pakala.</tp> <eq></eq> <eng>The tool is broken.</eng><br>
<ex><tp>ken la ilo li pakala.</tp> <eq></eq> <eng>Maybe the tool is broken.</eng>

<p>Unfortunately, some students forget that <tp>ken la</tp> comes at the <u>beginning</u> of the sentence (Indeed, as you'll soon learn, all <tp>la</tp> phrases always come at the beginning of the sentence.), so don't make that mistake.

<p>Here are a few more examples:<br>
<ex><tp>ken la jan Lisa li jo e ona.</tp> <eq></eq> <eng>Maybe Lisa has it.</eng><br>
<ex><tp>ken la ona li lape.</tp> <eq></eq> <eng>Maybe he's alseep.</eng><br>
<ex><tp>ken la mi wile tawa ma Palata.</tp> <eq></eq> <eng>Maybe I should go to India.</eng>

<p><lt2><tp>la</tp> and Time</lt2>

<p>Before we learn to use <tp>tenpo</tp> with <tp>la</tp>, let's first look at some common phrases that use <tp>tenpo</tp>:<br>
<ex><tp>tenpo suno</tp> <eq></eq> <eng>sun time,</eng> i.e. day<br>
<ex><tp>tenpo pimeja</tp> <eq></eq> <eng>dark time,</eng> i.e. night<br>
<ex><tp>tenpo ni</tp> <eq></eq> <eng>this time,</eng> i.e. the current time, the present<br>
<ex><tp>tenpo suno ni</tp> <eq></eq> <eng>this sun time,</eng> i.e. today<br>
<ex><tp>tenpo pimeja ni</tp> <eq></eq> <eng>this dark time,</eng> i.e. tonight<br>
<ex><tp>tenpo kama</tp> <eq></eq> <eng>coming time,</eng> i.e. the future<br>
<ex><tp>tenpo kama lili</tp> <eq></eq> <eng>little coming time,</eng> i.e. soon<br>
<ex><tp>tenpo pini</tp> <eq></eq> <eng>past time,</eng> i.e. the past<br>
<ex><tp>tenpo suno pini</tp> <eq></eq> <eng>past sun time,</eng> i.e. yesterday<br>
<ex><tp>tenpo pimeja pini</tp> <eq></eq> <eng>past dark time,</eng> i.e. last night<br>
<ex><tp>tenpo suno kama</tp> <eq></eq> <eng>coming sun time,</eng> i.e. tomorrow<br>
<ex><tp>tenpo mute</tp> <eq></eq> <eng>many times,</eng> i.e. often<p>

<p>To say when something happened, use the structure "<tp>tenpo ___ la</tp> [...]."<br>
<ex><tp>tenpo pini la mi weka.</tp> <eq></eq> <eng>In the past, I was away.</eng><br>
<ex><tp>tenpo ni la mi lon.</tp> <eq></eq> <eng>At this time, I am here.</eng><br>
<ex><tp>tenpo kama la mi lape.</tp> <eq></eq> <eng>In the future, I'll sleep.</eng><br>
<ex><tp>tenpo pimeja pini la mi kama nasa.</tp> <eq></eq> <eng>Last night, I got drunk.</eng><br>
<ex><tp>tenpo suli la jan Metusela li lon.</tp> <eq></eq> <eng>Methuselah lived a long time.</eng><br>
<ex><tp>tenpo suno pi nanpa tu wan la jan Jesu li sewi!</tp> <eq></eq> <eng>On the third day, Jesus rose!</eng>

<p>You can also use "<tp>tenpo ___ la</tp>" phrases to talk about your age, although Toki Pona's phrase for describing age is pretty quirky:<br>
<ex><tp>tenpo pi mute seme la sina sike e suno?</tp> <eq></eq> How old are you? (literally, <eng>Time of what amount [la] you circled the sun?</eng>)
The phrase may seem strange, but it's oddly accurate. Since the Earth takes one year to go around the sun, each birthday you've completed one trip around the Sun. To respond, just replace <tp>pi mute seme</tp> with your age:<br>
<ex><tp>tenpo luka luka luka luka luka tu tu la mi sike e suno.</tp> <eq></eq> I'm 29 years old.

<p><lt1>If ... then ...</lt1>

<p>Suppose that you want to say in English, <eng>If/when A happens (or were to happen), then B happens (or would happen), too.</eng> In Toki Pona, you'd say, "<tp>A la B</tp>."

<p>Here are some examples:<br>
<ex><tp>mama mi li moli la mi pilin ike.</tp> <eq></eq> <eng>If my parents were to die, I would feel bad.</eng><br>
<ex><tp>mi lape la ali li pona.</tp> <eq></eq> <eng>When I'm asleep, everything is okay.</eng><br>
<ex><tp>sina moku e telo nasa la sina nasa.</tp> <eq></eq> <eng>If you drink beer, you'll be silly.</eng><br>
<ex><tp>sina wawa la sina ken pakala e jan ike.</tp> <eq></eq> <eng>If you're strong, you can hurt bad guys.</eng><br>
<ex><tp>sina moli la sina ken ala toki.</tp> <eq></eq> <eng>If you are dead, you can't speak.</eng><br>
<ex><tp>sina sona e jan Jesu la ni li pona tawa mama sewi sina.</tp> <eq></eq> <eng>If you know Jesus, this is pleasing to your heavenly father.</eng><br>
<ex><tp>sina unpa la sina ken pali e jan lili.</tp> <eq></eq> <eng>If you have sex, you can make a baby.</eng>

<p><lt1>Miscellaneous</lt1>

<p>In this lesson's miscellaneous section, we'll learn how to express comparatives and superlatives in Toki Pona. (If you don't know what a "comparative" and "superlative" are, it's basically what you're doing in English when you say <tp>more</tp> and <tp>most</tp> or when you add <tp>-er</tp> and <tp>-est</tp> to a word.)

To express comparatives or superlatives in Toki Pona, you have to split your idea into two separate sentences. Let's suppose, for example, that you want to say that Mexico is better than Afghanistan. In Toki Pona, it'd be:<br>
<ex><tp>ma Mesiko li pona mute. ma Akanisan li pona lili.</tp> <eq></eq> literally, <eng>Mexico is much good. Afghanistan is a little good.</eng>

<p>Here are two more examples:<br>
<ex><tp>mi suli mute. sina suli lili.</tp> <eq></eq> I'm bigger than you. (literally, <eng>I'm very big. You're a little big.</eng>)<br>
<ex><tp>mi moku mute. sina moku lili.</tp> <eq></eq> I eat more than you. (literally, <eng>I eat a lot. You eat a little.</eng>)

