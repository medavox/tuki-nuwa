<p><lt1><tp>anpa</tp>, <tp>insa</tp>, <tp>monsi</tp>, <tp>poka</tp>, and <tp>sewi</tp></lt1>

<p>Although you may be tempted to use these words as prepositions, they're actually nouns. You have to use another preposition (usually <tp>lon</tp> or <tp>tawa</tp>) along with them. Some examples:<br>
<ex><tp>ona li lon sewi mi.</tp> <eq></eq> <eng>He is in my above,</eng> i.e. he is above me.</eng><br>
<ex><tp>pipi li lon anpa mi.</tp> <eq></eq> <eng>The bug is underneath me.</eng><br>
<ex><tp>moku li lon insa mi.</tp> <eq></eq> <eng>Food is inside me.</eng><br>
<ex><tp>len li lon monsi mi.</tp> <eq></eq> <eng>Clothes are behind me.</eng><br>
<ex><tp>mi moku lon poka sina.</tp> <eq></eq> <eng>I'm eating beside [or with] you.</eng><br>
<ex><tp>sina lukin e ona lon poka mi.</tp> <eq></eq> <eng>You'll see him next to me.</eng><br>
These words are nouns, and <tp>mi</tp> is a possessive pronoun meaning <eng>my.</eng> So <tp>sewi mi</tp> means something like <eng>my above</eng> or <eng>the area that is above me.</eng> To reiterate: Since these words are merely nouns, you must use them with a preposition (or a verb that doubles as a preposition). In all of the above examples, <tp>lon</tp> serves as the preposition. Don't forget to include a preposition!

<p>For now, you'll only be able to use these words for very simple prepositional phrases, basically just ones that end with only one word. (In other words, you haven't yet learned how to translate a sentence like, "I am above the big house." For now you can only say, "I am above the house," or "I'm above it.") To make more complex phrases, you'll need to learn about the word <tp>pi,</tp> which will be taught in <a href="okamasona11.php">lesson 11</a>.

<p>Since these words aren't prepositions, they're free for other uses, just like any other noun/adjective/verb: <tp>monsi</tp> can mean the back side of the body or the buttocks. <tp>anpa</tp> can be a verb:<br>
<ex><tp>mi anpa e jan utala.</tp> <eq></eq> <eng>I defeated the warrior.</eng>

<p><lt1><tp>sama</tp></lt1>

<p><tp>sama</tp> can be used for several different parts of speech, but it's easy to understand. Let's look at a few examples:<br>
<ex><tp>jan ni li sama mi.</tp> <eq></eq> <eng>That person is like me.</eng><br>
<ex><tp>ona li lukin sama pipi.</tp> <eq></eq> <eng>He looks like a bug.</eng><br>
<ex><tp>sama li ike.</tp> <eq></eq> <eng>Equality is bad.</eng>

<p>By the way, <tp>jan sama</tp> is the generally accepted phrase for <eng>sibling.</eng>

<p><lt1><tp>tan</tp></lt2>

<p>It's too early to learn everything you need to know about <tp>tan</tp>, but since it's the last Toki Pona preposition, let's at least learn a little about it now. Depending how it's used in a sentence, <tp>tan</tp> basically means either <eng>because of</eng> or <eng>from.</eng> (Rarely, you may see <tp>tan</tp> as a noun, but we'll discuss that in the <a href="okamasona8.php">next lesson</a>.)

<p>Let's learn from some examples:<br>
<ex><tp>mi moku tan ni: mi wile moku</tp> <eq></eq> This literally says, <eng>I eat because-of this: I want to-eat.</eng> A more colloquial translation would be, <eng>I eat because I'm hungry.</eng><br>
<ex><tp>mi tan ma ike.</tp> <eq></eq> <eng>I'm from a bad country.</eng>

