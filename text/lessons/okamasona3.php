<p><lt1>Sentences with <tp>mi</tp> or <tp>sina</sp> as the subject</lt1>

<p>One of the first principles you'll need to learn about Toki Pona is that it has no form of the verb <eng>to be.</eng> For example:<br>
<ex><tp>mi pona.</tp> <eq></eq> <eng>I <imp>am</imp> good.</eng><br>
<ex><tp>sina suli.</tp> <eq></eq> <eng>You <imp>are</imp> big/important.</eng><br>
Simply state <tp>mi</tp> or <tp>sina</tp>, then complete the sentence with an adjective or verb.  Although the lack of <eng>to be</eng> may seem odd and discomforting, it's actually simpler if you think about it, and there are other languages that don't use <eng>to be.</eng> Just practice until you get accustomed to the idea.

<p><lt1>Toki Pona Is Often Ambiguous</lt1>

<p>While reading the vocabulary section at the top of the page, you probably noticed that several words have multiple meanings. <tp>suli</tp>, for example, can mean either long or tall... or big... or important. You may be wondering how one word mean so many different things, but truthfully, <i>many</i> Toki Pona words have multiple meanings. With such a tiny vocabulary, ambiguity is inevitable but not necessarily bad: The vagueness forces you to focus on very basic, core features instead of lots of tiny, often frivolous details.

<p>Toki Pona's vocabulary is also ambiguous because it doesn't let you specify whether a word is singular or plural. For example, <tp>jan</tp> can mean either <eng>person</eng> or <eng>people.</eng> (Side note: Japanese is the same way.) You'll eventually learn a way to distinguish singular versus plural, but not in this lesson.

<p><lt1>The "Verb"</lt1>

<p>Look over these examples:<br>
<ex><tp>mi moku.</tp> <eq></eq> <eng>I eat.</eng> <b>or</b> <eng>I <imp>am</imp> food.</eng><br>
<ex><tp>sina pona.</tp> <eq></eq> <eng>You <imp>are</imp> good.</eng> <b>or</b> <eng>You fix.</eng></ex><br>
These sentences illustrate Toki Pona's ambiguity yet again. Because Toki Pona lacks <eng>to be,</eng> the exact meaning is lost. In this sentence, <tp>moku</tp> could be either a verb or a noun. Likewise, <tp>pona</tp> could be either an adjective or a verb. In these situations, the listener/reader must rely on context. After all, how often do you hear someone say <eng>I am food?</eng> I hope not very often! You can be fairly certain that <tp>mi moku</tp> means <eng>I'm eating.</eng> For sentences like <tp>sina pona</tp>, there is a way to help you clarify the intended meaning, but you'll learn about that in the next lesson.

<p><lt2>No Verb Tenses</lt2>

<p>Toki Pona's "verbs" have no tense.<br>
<ex><tp>mi pona.</tp> <eq></eq> <eng>I <imp>am</imp> good.</eng> <b>or</b> <eng>I <imp>was</imp> good.</eng> <b>or</b> <eng>I <imp>will be</imp> good.</eng><br>
This is yet another example of the vagueness inherent in Toki Pona. If absolutely necessary, there's a way to specify when something happened, but you won't learn about that until <a href="okamasona17.php">lesson 17</a>.

<p><lt1>Sentences without mi or sina as the subject</lt1>
<p>We've already looked at <tp>mi</tp> and <tp>sina</tp> sentences, which are the simplest sentences possible in Toki Pona. For sentences that don't use <tp>mi</tp> or <tp>sina</tp> as the subject, there is one small difference you'll need to learn. Look at how <tp>li</tp> is used:<br>
<ex><tp>telo li pona.</tp> <eq></eq> <eng>Water <imp>is</imp> good.</eng><br>
<ex><tp>suno li suli.</tp> <eq></eq> <eng><imp>The</imp> sun <imp>is</imp> big.</eng><br>
<ex><tp>moku li pona.</tp> <eq></eq> <eng>Eating/food <imp>is</imp> good.</eng>
<p><tp>li</tp> is a grammatical word that separates the subject from its "verb." Remember: Always use <tp>li</tp> if the subject isn't <tp>mi</tp> or <tp>sina</tp>, and never use it if the subject is <tp>mi</tp> or <tp>sina</tp>. Although <tp>li</tp> might seem cumbersome and worthless now, as you continue to learn Toki Pona you'll see that some sentences could be very confusing if <tp>li</tp> weren't there, so practice using it until it becomes normal to you. 

