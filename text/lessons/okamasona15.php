<p>Also remember from <a href="okamasona5.php">lesson 5</a> that <tp>lawa</tp> as a noun means <eng>head,</eng> and from <a href="okamasona7.php">lesson 7</a> that <tp>monsi</tp> means <eng>rear,</eng> <eng>back,</eng> etc.

<p>&nbsp;<p>

<p>All of the vocabulary words in this lesson are used to describe various body parts or bodily fluids, but most of the words have other uses, too. Below we'll look at each word in turn.

<p><lt2><tp>ko</tp></lt2>

<p><tp>ko</tp> is any type of semi-solid substance, but despite that open-ended definition, <tp>ko</tp> is seldom used in Toki Pona. When you do see it, it's usually in a compound phrase.

<p>Toki Ponans have generally accepted <tp>ko jaki</tp> as the standard phrase for <eng>feces,</eng> and <tp>pana e ko jaki</tp> for <eng>defecate.</eng>

<p><lt2><tp>kute</tp></lt2>

<p>As a verb, <tp>kute</tp> means <eng>to hear</eng> or <eng>to listen to.</eng><br>
<ex><tp>mi kute e sina.</tp> <eq></eq> <eng>I hear you.</eng><br>
<ex><tp>mi kute e kalama musi.</tp> <eq></eq> <eng>I'm listening to music.</eng>

<p>Although the <a href="dictionary.php">official dictionary</a> also defines <tp>kute</tp> as <eng>ear,</eng> you'll more often see the phrase <tp>nena kute</tp>. (As explained below, <tp>nena</tp> refers to any type of lump, mound, or other extrusion. The human ear, of course, protrudes from the side of the head.)

<p><lt2><tp>linja</lt2></tp></lt2>

<p><tp>linja</tp> means a line or any long, flexible thing like a rope, cord, or sewing thread.

<p><tp>linja lawa</tp> (literally, <eng>head line</eng>) is the generally accepted phrase for hair.

<p><lt2><tp>luka</tp></lt2>

<p><tp>luka</tp> means both <eng>hand</eng> and <eng>arm.</eng> There's no way to specify between the two.

<p>You may recall from <a href="okamasona5.php">lesson 5</a> that <tp>len</tp> means <eng>clothing.</eng> Therefore, <tp>len luka</tp> <eq></eq> mittens, gloves, etc. (literally, <eng>hand clothing</eng>).

<p><tp>luka</tp> is also used for Toki Pona's numbering system, but that will be taught in the <a href="okamasona16.php">next lesson</a>.

<p><lt2><tp>lupa</tp></lt2>

<p><tp>lupa</tp> is any kind of hole or opening. <eng>Door</eng> and <eng>window</eng> are both <tp>lupa</tp>.<br>
<ex><tp>o open e lupa!</tp> <eq></eq> <eng>Open the door!</eng>

<p><tp>lupa</tp> may also be used to describe the vagina and the anus. If context isn't sufficient to clarify what you're describing with just <tp>lupa</tp> alone, you may add other descriptors, viz: <tp>lupa meli</tp> for <eng>vagina,</eng> and <tp>lupa monsi</tp> for <eng>anus.</eng>

<p><lt2><tp>nena</tp></lt2>

<p><tp>nena</tp> is basically any type of protrusion, from the very small (e.g., a button on a shirt, or small bump or mole on your skin) to the very large (a mountain).

<p><tp>nena</tp> may describe the nose. <tp>nena sike</tp> or <tp>nena sike meli</tp> is often used to describe a woman's breasts. (<tp>sike</tp> will be discussed a little more below.)

<p><lt2><tp>noka</tp></lt2>

<p><tp>noka</tp> means both <eng>leg</eng> and <eng>foot.</eng> Similar to <eng>luka,</eng> you can combine <tp>noka</tp> with <tp>len</tp>: <tp>len noka</tp> is generally used to mean <eng>shoe,</eng> but it can also mean <eng>pants,</eng> <eng>socks,</eng> or any other covering for the leg or foot.

<p><lt2><tp>oko</tp></lt2>

<p>As previously noted in <a href="okamasona4.php">lesson 4</a>, the <a href="dictionary.php">official dictionary</a> lists <tp>oko</tp> and <tp>lukin</tp> as being interchangeable. Traditionally, though, these two words had different connotations and uses, and most Toki Ponans seem to use these two words according to their traditional definitions, so this course teaches them that way.</small>

<p>Traditionally, <tp>oko</tp> was basically a noun that meant <eng>eye</eng> (or as an adjective, <eng>ocular</eng>), while <tp>lukin</tp> was basically a verb that meant <eng>look at,</eng> <eng>see,</eng> etc.

<p><lt2><tp>palisa</tp></lt2>

<p><tp>palisa</tp> is any type of stick, rod, pole, or any similar object. A tree limb would be <tp>palisa</tp>, for example.

<p><tp>palisa mije</tp> is the generally accepted phrase for <eng>penis.</eng>

<p><lt2><tp>selo</tp></lt2>

<p><tp>selo</tp> is any type of covering or outer form. Tree bark would be one example of <tp>selo</tp>. If you're discussing a human, <tp>selo</tp> would refer to his or her skin.

<p><lt2><tp>sike</tp></lt2>

<p><tp>sike</tp> means <eng>ball</eng> or any other spherical or semi-spherical object. As an adjective, it means <eng>circular</eng> or <eng>round.</eng>

<p>As noted above, a woman's breasts may be <tp>nena sike meli</tp>. A man's testicles may be <tp>sike mije</tp>.

<p><lt2><tp>sinpin</tp></lt2>

<p><tp>sinpin</tp> is basically any vertical surface. A wall is the best example of a <tp>sinpin</tp>.

<p><tp>sinpin</tp> may also be used to describe the front side of one's body, especially the chest and/or face.

<p>Side note: Remember that <tp>monsi</tp> means the back or the back side.

<p><lt2><tp>uta</tp></lt2>

<p><tp>uta</tp> means <eng>mouth</eng> or (as an adjective) <eng>oral.</eng>

<p><tp>kiwen uta walo</tp> (literally, <eng>white mouth stones</eng) is generally used to mean <eng>teeth.</eng>

<p><lt2>Body Fluids</lt2>

<p>You learned above that <tp>ko jaki</tp> means <eng>feces</eng> and that <tp>pana e ko jaki</tp> means <eng>defecate.</eng>

<p>Other useful phrases include:<br>
<ex><tp>telo walo mije</tp> <eq></eq> semen (literally, <eng>man's white fluid</eng>). If the context is already clear, you may omit <tp>walo</tp> and/or <tp>mije</tp>.<br>
<ex><tp>telo sijelo loje</tp> <eq></eq> blood (literally, <eng>red body fluid</eng>)<br>
<ex><tp>telo jelo</tp> <eq></eq> urine (literally, <eng>yellow fluid</eng>)

<p>You may also use <tp>pana e</tp> with these phrases, too. For example, <tp>pana e telo sijelo loje</tp> means <eng>bleed.</eng>

