<p><lt1>Questions using <tp>seme</tp></lt1>

<p>In <a href="okamasona8.php">lesson 8</a>, you learned how to ask and answer yes/no questions. For more complex questions, though, you must use the word <tp>seme</tp>.<br>
<ex><tp>seme li utala e sina?</tp> <eq></eq> <eng>What attacked you?</eng><br>
<ex><tp>seme li moku e kili mi?</tp> <eq></eq> <eng>What is eating my fruit?</eng><br>
<ex><tp>seme li lon poka mi?</tp> <eq></eq> <eng>What is beside me?</eng><br>
<ex><tp>seme li lon tomo mi?</tp> <eq></eq> <eng>What is in my house?</eng><br>
<ex><tp>seme li pona tawa sina?</tp> <eq></eq> <eng>What is good to you? What do you like?</eng>

<p>To ask a question in English, you have to move the question word (i.e., what, who, when, where, why, how) to the beginning of the sentence. In Toki Pona, though, the word order doesn't change. For example:<br>
<ex><tp>sina lukin e ijo.</tp> <eq></eq> <eng>You're watching something.</eng><br>
<ex><ex> <font color=ff7777>&rarr;</font> <tp>sina lukin e seme?</tp> <eq></eq> <eng>You're watching what?</eng> In colloquial English, you'd ask, <eng>What are you watching?</eng><br>
<ex><ex>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Do <b>not</b> say, "<tp>seme lukin sina?</tp>," "<tp>seme sina lukin?</tp>," etc.<br>
Both <tp>ijo</tp> and <tp>seme</tp> are treated as plain nouns, and the sentence's word order doesn't change even if the sentence is a question. Here are some more examples:<br>
<ex><tp>sina pakala e seme?</tp> <eq></eq> <eng>What did you destroy?</eng><br>
<ex><tp>sina lon seme?</tp> <eq></eq> <eng>Where are you?</eng> (literally: <eng>You in what?</eng>)<br>
<ex><tp>ona li jo e seme?</tp> <eq></eq> <eng>What does he have?</eng>

<p><tp>seme</tp> can also be an adjective. The word order of the sentence still doesn't change, no matter what:<br>
<ex><tp>jan seme li moku?</tp> <eq></eq> <eng>Who is eating?</eng><br>
<xp><tp>jan seme</tp> is literally translated as <eng>person what/which</eng>, but it means <eng>who.</eng></xp><br>
<ex><tp>jan seme li tawa lon poka sina?</tp> <eq></eq> <eng>Who went with you?</eng><br>
<ex><tp>sina lukin e jan seme?</tp> <eq></eq> <eng>Whom did you see?</eng><br>
<ex><tp>sina toki tawa jan seme?</tp> <eq></eq> <eng>Whom are you talking to?</eng><br>
<ex><tp>ma seme li pona tawa sina?</tp> <eq></eq> <eng>Which countries are good to you? Which countries do you like?</eng><br>
<ex><tp>sina kama tan ma seme?</tp> <eq></eq> <eng>Which country do you come from?</eng> (literally, <eng>You come from country which?</eng>)

<p>Because English and many other languages re-arrange words for question sentences, you may be tempted to do likewise in Toki Pona. One helpful trick to avoid doing so is to re-imagine the sentence as a plain statement by replacing <tp>seme</tp> with the word <tp>ni</tp>. If the sentence doesn't make sense after you replace <tp>seme</tp> with <tp>ni</tp>, you've probably done something wrong.

<p><tp>seme</tp> is also used to say <eng>why</eng>. Remember that <tp>tan</tp> can mean <eng>because of,</eng> and therefore <tp>tan seme</tp> <eq></eq> <eng>because of what</eng> <eq></eq> <eng>why.</eng><br>
<ex><tp>sina ike tan seme?</tp> <eq></eq> <eng>You're bad because-of what?</eng> or in other words, <eng>Why are you bad?</eng><br>

<p><lt1>Miscellaneous</lt1>

<p>You're now over half way through this course. Congratulations! There are still two important grammar concepts to learn (namely, <tp>pi</tp> and <tp>la</tp>), but other than that, you now know all of Toki Pona's essential features. The remainder of the course will begin to wrap things up and teach various lesser topics.

<p>Some of the remaining words and details are simply too easy to justify teaching them in a lesson by themselves. Therefore, in some of the remaining lessons (but not in all of them), you'll have a miscellaneous section like this one to introduce a few vocabulary words and give some examples how to use them. In this lesson's miscellaneous section, you'll learn about the words <tp>supa</tp>, <tp>suwi</tp>, <tp>sin</tp>, and <tp>olin</tp>.

<p><lt2><tp>supa</tp></lt2>

<p>The <a href="dictionary.php">official dictionary</a> defines <tp>supa</tp> as any type of horizontal surface. Although this definition is accurate, <tp>supa</tp> is used in particular to mean a piece of furniture, especially a table, a chair, or sofa, and we Toki Ponans also use <tp>supa lape</tp> (literally, <eng>sleep surface</eng>) to mean <eng>bed.</eng>

<p><lt2><tp>suwi</tp></lt2>

<p>As an adjective, <tp>suwi</tp> means <tp>sweet</tp> or <tp>cute</tp>. (Don't confuse <tp>suwi</tp> with sexual attractiveness. Just as you learned in <a href="okamasona5.php">lesson 5</a> that <tp>pona lukin</tp> <eq></eq> <eng>pretty,</eng> likewise <tp>unpa lukin</tp> <eq></eq> <eng>sexy.</eng>) As a noun, <tp>suwi</tp> means <tp>candy</tp> or some other sweet food. Here are a few examples:<br>
<ex><tp>jan lili sina li suwi.</tp> <eq></eq> <eng>Your baby is cute.</eng><br>
<ex><tp>telo kili ni li suwi.</tp> <eq></eq> <eng>This fruit drink is sweet.</eng><br>
<ex><tp>mi wile e suwi!</tp> <eq></eq> <eng>I want a cookie!</eng>

<p><lt2><tp>sin*</tp></lt2>

<p>This word is almost always an adjective and simply means <tp>another</tp> or <tp>more</tp>. Here are two examples:<br>
<ex><tp>jan sin li kama.</tp> <eq></eq> <eng>More people are coming.</eng> <b>or</b> <eng>Another person is coming.</eng><br>
<ex><tp>mi wile e suwi sin!</tp> <eq></eq> <eng>I want another cookie!</eng> <b>or</b> <eng>I want more cookies!</eng>

<p><small>* The <a href="dictionary.php">official dictionary</a> lists <tp>sin</tp>, which has been a mainstay word ever since Toki Pona was first presented online, as being interchangeable with <tp>namako</tp>, a word made official more than ten years later. Traditionally, though, these two words had different connotations and uses, and most Toki Ponans seem to use these two words according to their traditional definitions, so this course will teach them that way. <tp>namako</tp> is explained more fully in <a href="okamasona18.php">lesson 18</a>.</small>

<p><lt2><tp>olin</tp></lt2>

<p><tp>olin</tp> means <eng>love</eng> but only refers to affectionate love, e.g. for loving people. You could <tp>olin</tp> your parents or girlfriend but not baseball. Since you can't <tp>olin</tp> things or objects, instead express your feelings using the phrase "<tp>pona tawa mi</tp>":<br>
<ex><tp>ni li pona tawa mi.</tp> <eq></eq> <eng>That (is) good to me,</eng> or in other words, <eng>I like that.</eng>

