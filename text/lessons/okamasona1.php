<div class="p">

<p>toki! I'm glad you've decided to give these lessons a look. Hopefully this introduction will answer any questions you may have. If you're thinking about learning Toki Pona, hopefully this introduction will also give you a feel for what lies ahead.

<p>Although Toki Pona has a modest following, including a few more or less fluent speakers, I urge you to consider Toki Pona not so much for how well it can entertain you or how large of a following it has, but more for the insight it can give you as it helps you develop a simpler way of thinking.

<p>If you're like me, as you learn Toki Pona you may find that the frame of mind that Toki Pona relies upon sometimes conflicts with how you've always approached understanding things. It's not so easy to adopt the way of thinking that Toki Pona requires. But be patient; eventually, even if you don't agree with the ideas behind Toki Pona, it will at least broaden your mind and help you understand things better.

<p>You should now be able to decide whether or not you'd like to learn Toki Pona. A last word of advice: Don't feel like you're alone in learning the language. I've helped others learn the language in the past (<a href="contact.php">Contact me!</a>), and there are plenty of other people willing to explain tough concepts and give advice.

<p>With that said, enjoy the lessons, and good luck learning Toki Pona!

