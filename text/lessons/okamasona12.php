<p><lt1><tp>anu</tp>, <tp>en</tp>, and <tp>taso</tp></lt1>

<p>Toki Pona only has three conjunction: <tp>anu</tp>, <tp>en</tp>, and <tp>taso</tp>. However, each is used differently, so let's look at them one at a time.

<p><lt2><tp>anu</tp></lt2>

<p>This word can be used to make questions when there's a choice between two different options. For example, if you came home to find that someone had eaten all of the cookies, and you know that the person who ate them has to be either Susan or Lisa, you might ask:<br>
<ex><tp>jan Susan anu jan Lisa li moku e suwi?</tp><br>
Semi-literally, this sentence reads, <eng>Susan or Lisa ate the cookies?</eng> In colloquial English, it reads, "Did Susan eat the cookies, or was it Lisa?" As you see, you can't necessarily translate directly from English, especially with <tp>anu</tp>. Think independently. Here are more examples:<br>
<ex><tp>sina jo e kili anu telo nasa?</tp> <eq></eq> <eng>Do you have the fruit, or is it the wine that you have?</eng><br>
<ex><tp>sina toki tawa mi anu ona?</tp> <eq></eq> <eng>Are you talking to me, or are you talking to him?</eng><br>
<ex><tp>ona anu jan ante li ike?</tp> <eq></eq> <eng>Is he bad, or is it the other person who's bad?</eng><br>
<ex><tp>sina toki pi mama anu jan lili?</tp> <eq></eq> <eng>Are you talking about the parent, or is the child that you're talking about?</eng>

<p>You can make another type of question with <tp>anu</tp>, too. In English you may sometimes say, "So are you coming or what?" ... Well, you can say something similar in Toki Pona:<br>
<ex><tp>sina kama anu seme?</tp> <eq></eq> <eng>Are you coming or what?</eng> (literally: <eng>You come or what?</eng>)<br>
<ex><tp>sina wile moku anu seme?</tp> <eq></eq> <eng>Do you want to eat or what?</eng><br>
<ex><tp>sina wile e mani anu seme?</tp> <eq></eq> <eng>Do you want the money or what?</eng><br>
If you don't like this method, you can still ask questions using the method taught in <a href="okamasona8.php">lesson 8</a>.

<p><lt2><tp>en</tp></lt2>

<p>This word simply means "and". Use it to join two nouns together in a sentence's subject:<br>
<ex><tp>mi en sina li jan pona.</tp> <eq></eq> <eng>You and I are friends.</eng><br>
<ex><tp>jan lili en jan suli li toki.</tp> <eq></eq> <eng>The child and the adult are talking.</eng><br>
<ex><tp>kalama musi en meli li pona tawa mi.</tp> <eq></eq> <eng>I like music and girls.</eng>

<p>Don't use <tp>en</tp> to join two direct objects together, though. Instead use the multiple-<tp>e</tp> technique you learned in <a href="okamasona4.php">lesson 4</a>.<br>
<ex>Right: <tp>mi wile e moku e telo.</tp> <eq></eq> <eng>I want food and water.</eng><br>
<ex>Wrong: <tp>mi wile e moku en telo.</tp>

<p>Also, don't use <tp>en</tp> to connect two whole sentences, even though this is common in English. Instead use the multiple-<tp>li</tp> technique you learned in <a href="okamasona4.php">lesson 4</a>, or split the sentence into two sentences:<br>
<ex>Right: <tp>mi moku e kili li lukin e tomo.</tp> <eq></eq> <eng>I'm eating fruit and looking at the house.</eng><br>
<ex>Right: <tp>mi moku e kili. mi lukin e tomo.</tp> <eq></eq> <eng>I'm eating fruit. I'm looking at the house.</eng><br>
<ex>Wrong: <tp>mi moku e kili en lukin e tomo.</tp><br>
<ex>Wrong: <tp>mi moku e kili, en mi lukin e tomo.</tp>

<p>You can use <tp>en</tp> with <tp>pi</tp> if two people own something together:<br>
<ex><tp>tomo pi jan Keli en mije ona li suli.</tp> <eq></eq> <eng>The house of Keli and her husband is big.</eng><br>
<ex><tp>jan lili pi jan Ken en jan Melisa li suwi.</tp> <eq></eq> <eng>Ken's and Melisa's baby is sweet.</eng>

<p><lt2><tp>taso</tp></lt2>

<p><tp>taso</tp> can be used as an adjective or as a conjunction. First let's study its use as a conjunction.

<p>Using <tp>taso</tp> as a conjunction is really simple. Here are a few example:<br>
<ex><tp>mi wile moku. taso mi jo ala e moku.</tp> <eq></eq> <eng>I want to eat. But I don't have food.</eng><br>
<ex><tp>mi wile lukin e tomo mi. taso mi lon ma ante.</tp> <eq></eq> <eng>I want to see my house. But I'm in a different country.</eng><br>
Just remember to start a new sentence when you use <tp>taso</tp> as a conjunction. Unlike in English, you can't run everything together with a comma!<br>
<ex><tp>Right: mi pona. taso meli mi li pakala.</tp> <eq></eq> <eng>I'm okay. But my girlfriend is injured.</eng><br>
<ex><tp>Wrong: mi pona, taso meli mi li pakala.<br>
<ex><tp>Wrong: mi pona taso meli mi li pakala.<p>

As mentioned above, <tp>taso</tp> can be used as an adjective. It goes after the noun, just like all other adjectives in Toki Pona.<br>
<ex><tp>jan Lisa taso li kama.</tp> <eq></eq> <eng>Only Lisa came.</eng><br>
<ex><tp>mi sona e ni taso.</tp> <eq></eq> <eng>That's all I know.</eng> (literally, <eng>I know that only.</eng>)<br>
<tp>taso</tp> can also be an adverb.<br>
<ex><tp>mi musi taso.</tp> <eq></eq> <eng>I'm just joking.</eng><br>
<ex><tp>mi pali taso.</tp> <eq></eq> <eng>I just work.</eng> (In colloquial English, <eng>All I ever do is work.</eng>)<br>
<ex><tp>mi lukin taso e meli ni! ali li pona.</tp> <eq></eq> <eng>I only looked at that girl! Everything's okay.</eng>

<p><lt1><tp>kin*</tp></lt1>

<p><tp>kin</tp> means <eng>also,</eng> <eng>still,</eng> or <eng>indeed.</eng> Some examples:<br>
<ex>1. <tp>mi tawa ma Elopa.</tp> <eq></eq> <eng>I went to Europe.</eng><br>
<ex>&nbsp;&nbsp;&nbsp;&nbsp;<tp>pona! mi tawa kin.</tp> <eq></eq> <eng>Cool! I went too.</eng><br>
<ex>2. <tp>mi mute o tawa.</tp> <eq></eq> <eng>Let's go.</eng><br>
<ex>&nbsp;&nbsp;&nbsp;&nbsp;<tp>mi ken ala. mi moku kin.</tp> <eq></eq> <eng>I can't. I'm still eating.</eng><br>
<ex>3. <tp>a! sina lukin ala lukin e ijo nasa ni?</tp> <eq></eq> <eng>Whoa! Do you see that weird thing?</eng><br>
<ex>&nbsp;&nbsp;&nbsp;&nbsp;<tp>mi lukin kin e ona.</tp> <eq></eq> <eng>I see it indeed.</eng>

<p><small>* As previously noted in <a href="okamasona9.php">lesson 9</a>, the <a href="dictionary.php">official dictionary</a> lists <tp>a</tp> and <tp>kin</tp> as being interchangeable. Traditionally, though, these two words had different connotations and uses, and most Toki Ponans seem to use these two words according to their traditional definitions, so this course teaches them that way.</small>

<p><lt1><tp>pilin</tp></lt1>

<p>You learned <tp>pilin</tp> in <a href="okamasona6.php">lesson 6</a>, but we're going to look at it more closely and learn something new at the same time.

<p><lt2><tp>pilin</tp> and temperatures</lt2>

<p>In case you've forgotten, <tp>seli</tp> means <tp>hot</tp> or <tp>heat</tp>. We can use this word to talk about the weather. From the vocabulary at the beginning of this lesson, you also learned that <tp>lete</tp> means <tp>cold</tp>. We can use these words to describe the temperature:<br>
<ex><tp>seli li lon.</tp> <eq></eq> <eng>It's hot.</eng> (literally: <eng>Heat is present.</eng>)<br>
<ex><tp>lete li lon.</tp> <eq></eq> <eng>It's cold.</eng> (literally: <eng>Cold is present.</eng>)<p>

You can add <tp>lili</tp> and <tp>mute</tp> to be more specific.<br>
<ex><tp>seli mute li lon.</tp> <eq></eq> <eng>It's scorching.</eng> (literally: <eng>Much heat is present.</eng>)<br>
<ex><tp>seli lili li lon.</tp> <eq></eq> <eng>It's warm.</eng> (literally: <eng>Little heat is present.</eng>)<br>
<ex><tp>lete mute li lon.</tp> <eq></eq> <eng>It's freezing.</eng> (literally: <eng>Much cold is present.</eng>)<br>
<ex><tp>lete lili li lon.</tp> <eq></eq> <eng>It's cool.</eng> (literally: <eng>Little heat is present.</eng>)

<p>The above phrases only describe the temperature of the general surroundings/environment. To describe the temperature of a specific object, regardless of the surrounding environment, use <tp>pilin</tp>. For example, suppose you grab a tool, and the handle is cold. You could describe it this way:<br>
<ex><tp>ilo ni li lete pilin.</tp> <eq></eq> <eng>This axe is cold to-touch.</eng><br>
This structure is basically the same as <tp>pona lukin</tp>, which means <eng>pretty</eng> as you learned in <a href="okamasona5.php">lesson 5</a>. <tp>pilin</tp> is actually acting as an adverb here. A strict translation of the above sentence would be, <eng>This tool is touchily cold.</eng>

<p>Just like with the <tp>lon</tp> phrases, the <tp>pilin</tp> phrases can use <tp>mute</tp> and <tp>lili</tp> to intensify the descriptions:<br>
<ex><tp>ni li lete pilin mute.</tp> <eq></eq> <eng>This is very cold.</eng> (literally: <eng>This is much touchily cold.</eng>)<br>
<ex><tp>ni li seli pilin lili.</tp> <eq></eq> <eng>This is warm.</eng> (literally: <eng>This is a little touchily hot.</eng>)</eng>

<p><lt2><tp>pilin</tp> for emotions and thoughts</lt2>

<p>You also use <tp>pilin</tp> to describe how you're feeling.<br>
<ex><tp>mi pilin pona.</tp> <eq></eq> <eng>I feel good/happy.</eng><br>
<ex><tp>mi pilin ike.</tp> <eq></eq> <eng>I feel bad/sad.</eng><br>
<ex><tp>sina pilin seme?</tp> <eq></eq> <eng>How do you feel?</eng>

<p><tp>pilin</tp> can also mean <tp>to think</tp>:<br>
<ex><tp>mi pilin e ni: sina jan ike.</tp> <eq></eq> <eng>I think you're a bad person.</eng> (literaly: <eng>I think this: You're bad person.</eng>)<br>
If you ask someone, <eng>What are you thinking about?,</eng> the <eng>about</eng> is removed in Toki Pona:<br>
<ex><tp>sina pilin seme?</tp> <eq></eq> <eng>What are you thinking?</eng><br>
When you answer, use <tp>pi</tp> if needed:<br>
<ex><tp>mi pilin ijo.</tp> <eq></eq> <eng>I'm thinking <imp>about</imp> something.</eng><br>
<ex><tp>mi pilin pi meli ni.</tp> <eq></eq> <eng>I'm thinking about that woman.</eng>

<p>Because feelings and thoughts are related concepts, and because <tp>pilin</tp> encompasses both, sometimes using <tp>sona</tp> or <tp>kama sona</tp> instead may help improve clarity when describing thoughts. For example:<br>
<ex><tp>mi kama sona e tomo.</tp> <eq></eq> <eng>I'm thinking about the house.</eng> (literally: <eng>I'm coming to understand the house.</eng>)

