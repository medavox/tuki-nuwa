<p><lt1>Ambiguity of the Names of Living Things</lt1>

<p>As you've seen repeatedly over the past thirteen lessons, Toki Pona's vocabulary is often vague. The vocabuarly for living things is no different. Below we'll look at each of the words that Toki Pona has for living things, and we'll learn a few things about each as we go along.

<p><lt2><tp>akesi</tp></lt2>

<p><tp>akesi</tp> describes all reptiles and amphibians, including geckoes, lizards, alligators, snakes, iguanas, dinosaurs, and turtles.

<p>If necessary, you may use adjectives (with or without <tp>pi</tp>) to help specify a particular type or species of <tp>akesi</tp>. For example, <tp>akesi pi telo moli</tp> (<eng>reptile/amphibian of deadly liquid</eng>) might mean venomous snakes or poisonous frogs. <tp>akesi suli</tp> (<eng>big reptile</eng>) might mean dinosaur.

<p>Monsters--which are often depicted as slimey with weird skin--would also generally be considered <tp>akesi</tp>. Be aware that Sonja Lang, the creator of Toki Pona, once proposed a new word <tp>monsuta</tp> that tentatively was to mean <eng>monster</eng> and perhaps also <eng>fear,</eng> but that word wasn't included in the <a href="dictionary.php">dictionary</a> in Sonja's official book, <i><a href="http://www.amazon.com/gp/product/0978292308">Toki Pona: The Language of Good</a></i>. I mention <tp>monsuta</tp> here because you may see it in some older texts, and because a few speakers may continue using it even though it's deprecated.

<p><lt2><tp>kala</tp></lt2>

<p><tp>kala</tp> describes fish and other related animals that live in water.

<p>In rare circumstances, you can also use <tp>kala</tp> as an adjective. For example, <tp>jan kala</tp> <eq></eq> <eng>mermaid.</eng>

<p><lt2>kasi</lt2>

<p><tp>kasi</tp> describes <b>all</b> plants and plant-like things, from the tallest tree to small fungi.

<p><tp>kasi</tp> is used in many compound phrases, for example:<br>
<ex><tp>kasi kule</tp> <eq></eq> flower (literally, <eng>colorful plant</eng>)<br>
<ex><tp>kasi anpa</tp> <eq></eq> grass, etc. (literally, <eng>ground plant</eng>)<br>
<ex><tp>kasi nasa</tp> or <tp>kasi sona</tp> <eq></eq> marijuana, depending on whether you want to emphasize its recreational (<tp>kasi nasa</tp>) or meditative/contemplative (<tp>kasi sona</tp>) traits<br>
<ex><tp>kasi suli</tp> <eq></eq> trees, large shrubs, etc.<br>
<ex><tp>kasi lili</tp> <eq></eq> weeds, etc.<br>
<ex><tp>ma kasi</tp> <eq></eq> forest, jungle

<p><lt2>pipi</lt2>

<p><tp>pipi</tp> describes all types of bugs, including spiders, ants, roaches, and butterflies. 

<p><lt2>soweli</lt2>

<p><tp>soweli</tp> describes basically all land mammals, including bears, cats, cows, dogs, elephants, giraffes, horses, kangaroos, lions, mice, raccoons, sheep, and zebras.

If you're eating meat from a <tp>soweli</tp> (e.g., beef or pork), it's still <tp>soweli</tp>. There's no separate word for meat. By the way, <tp>pona moku</tp> <eq></eq> <eng>tasty,</eng> <eng>delicious,</eng> etc.<br>
<ex><tp>soweli ni li pona moku.</tp> <eq></eq> <eng>This steak is tasty.</eng><br>

<p><lt2>waso</lt2>

<p><tp>waso</tp> basically includes all birds and similar flying animals (e.g., bats).

<p>Like <tp>soweli</tp>, meat from a bird is still called <tp>waso</tp>.<br>
<ex><tp>mi mute o moku e waso lon tomo Chik-Fil-A.</tp> <eq></eq> <eng>Let's eat chicken at Chik-Fil-A.</eng>

<p><lt1><tp>moli</tp></lt1>

<p><tp>moli</tp> is straight-forward, but let's look at a few examples.<p>As an adjective, <tp>moli</tp> means <eng>dead.</eng><br>
<ex><tp>pipi li moli.</tp> <eq></eq> <eng>The beetle is dead.</eng><br>
<ex><tp>jan moli li toki ala.</tp> <eq></eq> <eng>Dead people don't talk.</eng>

<p><tp>kama moli</tp> <eq></eq> <eng>dying</eng>. Remember that <tp>kama</tp> gives a verb or adjective a progressive quality, so <tp>kama moli</tp> is similar to phrases you learned in earlier lessons, e.g. <tp>kama sona</tp> (<eng>learn</eng>) and <tp>kama jo</tp> (<eng>get</eng>).<br>
<ex><tp>jan utala pakala li kama moli.</tp> <eq></eq> <eng>The injured soldier is dying.</eng><br>
<ex><tp>soweli pi kama moli li kalama ike.</tp> <eq></eq> <eng>The dying lion makes a terrible noise.</eng>

<p><tp>moli</tp> can also be a verb, of course.<br>
<ex><tp>jan li moli e waso.</tp> <eq></eq> <eng>The person killed the bird.</eng>

