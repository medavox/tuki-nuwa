In previous lessons, you learned <tp>ali</tp> (<eng>all</eng> or <eng>every,</eng> from <a href="okamasona8.php">lesson 8</a>) and <tp>mute</tp> (<eng>many,</eng> from <a href="lesson5.php">lesson 5</a>), but you'll learn new uses of these two words in this lesson, namely:

<p><table class="ldict">
  <tr>
  <tr>
    <td class="dt">ali</td>
     <td class="de">100</td></tr>
  <tr>
    <td class="dt">mute</td>
     <td class="de">20</td></tr></table>

<p><lt1>Two Numbering Systems</lt1>

<p>Toki Pona has two complementary yet somewhat different approaches to expressing numbers: one simpler, more traditional approach; and a more recent, more complex approach. Toki Ponans use both, so let's study each in turn.

<p><lt1>Traditional Numbering</lt1>

<p>For about ten years, Toki Pona basically only had three numerical words: <tp>wan</tp> for 1, <tp>tu</tp> for 2, and (less frequently used) <tp>luka</tp> for 5.

<p>Higher numbers could be expressed by mathematically combining those three words into strings. For example, since 3 = 2 + 1, to express <eng>3</eng> in Toki Pona, you'd say <tp>tu wan</tp>. Likewise, 14 = 5 + 5 + 2 + 2 = <tp>luka luka tu tu</tp>.

<p>Here are lots of examples:<br>
<ex><tp>ijo luka tu pi pona lukin</tp> <eq></eq> <eng>7 pretty things</eng><br>
<ex><tp>akesi wan</tp> <eq></eq> <eng>1 snake</eng><br>
<ex><tp>kasi tu</tp> <eq></eq> <eng>2 trees</eng><br>
<ex><tp>nimi luka luka luka luka tu</tp> <eq></eq> <eng>22 names</eng><br>
<ex><tp>tenpo suno luka luka luka luka luka luka luka luka</tp> <eq></eq> <eng>40 days</eng><br>
<ex><tp>linja luka luka</tp> <eq></eq> <eng>10 lines</eng><br>
<ex><tp>jan pona luka luka tu</tp> <eq></eq> <eng>12 friends</eng><br>
<ex><tp>tomo tu wan</tp> <eq></eq> <eng>3 structures</eng><br>
<ex><tp>lupa luka tu</tp> <eq></eq> <eng>7 doors</eng><br>
<ex><tp>soweli tu tu</tp> <eq></eq> <eng>4 horses</eng>

<p>As you probably noticed with the above examples, expressing larger numbers (beyond 25 or so) quickly becomes so tedious and cumbersome that you must reconsider whether it's even worthwhile. True to its design goals, Toki Pona's original numbering system forced you to be simple-minded. 

<p>If specifying a large number was unnecessary, you could simply use <tp>mute</tp> (<eng>many</eng>) instead. To express truly massive numbers, just add more <tp>mute</tp>:<br>
<ex><tp>jan mute li kama.</tp> <eq></eq> <eng>Many people are coming.</eng><br>
<ex><tp>jan mute mute mute li kama!</tp> <eq></eq> <eng>Many, many, many people are coming!</eng>

<p>You could also use <tp>mute lili</tp> to mean <eng>some,</eng> i.e. more than just two but still not very many.<br>
<ex><tp>jan mute lili li kama.</tp> <eq></eq> <eng>A few people are coming.</eng>

<p><lt1>New Numbering</lt1>

<p>Some years later, Sonja Lang, the creator of Toki Pona, changed the numbering system by assigning specific quantities to the words <tp>mute</tp> (20) and <tp>ali</tp> (100). The original concept of adding numbers together still applies. Here are a few examples:<br>
<ex><tp>tenpo suno mute luka tu wan</tp> <eq></eq> <eng>28 days</eng><br>
<ex><tp>mute mute tu</tp> <eq></eq> <eng>42</eng><br>
<ex><tp>jan utala ali ali ali</tp> <eq></eq> <eng>300 soldiers</eng><br>
<ex><tp>ma ali mute mute mute mute luka luka luka wan</tp> <eq></eq> <eng>196 countries</eng>

<p>Of course, you can still use the old numbering system, as do many Toki Ponans. <tp>luka luka luka luka</tp> still means <eng>20.</eng>

<p><lt1>Ordinal Numbers</lt1>

<p>For ordinal numbers, just add <tp>pi nanpa</tp> between the noun and the number.<br>
<ex><tp>ni li jan lili ona pi nanpa tu wan.</tp> <eq></eq> <eng>This is her third child.</eng><br>
<ex><tp>meli mi pi nanpa tu li nasa!</tp> <eq></eq> <eng>My second girlfriend was crazy!</eng>

<p>Note: Some Toki Ponans don't use <tp>pi</tp> and simply use <tp>nanpa</tp> by itself. Sonja Lang's <a href="http://www.amazon.com/Toki-Pona-The-Language-Good/dp/0978292308">official Toki Pona book</a> also teaches the ordinal numbers without <tp>pi</tp>. I believe this structure to be incorrect, but I'm mentioning it here so that you'll recognize it when you see it.

<p><lt1>Other Uses of <tp>wan</tp> and <tp>tu</tp></lt1>

<p>Although not defined as such in the <a href="dictionary.php">official dictionary</a>, <tp>wan</tp> and <tp>tu</tp> are sometimes used as verbs.

<p><tp>tu</tp> can mean <eng>divide</eng> or <eng>split.</eng><br>
<ex><tp>o tu e palisa ni.</tp> <eq></eq> Break this stick into two pieces. (literally, <eng>Split this stick.</eng>)

<p><tp>wan</tp> can mean <eng>unite.</eng><br>
<ex><tp>mi en meli mi li wan.</tp> <eq></eq> My girlfriend and I got married. (literally, <eng>I and my girlfriend united.</eng>)<br>
Be aware that the <a href="dictionary.php">official dictionary</a> teaches that <tp>unpa</tp> means <eng>to be married to.</eng> However, as you probably recall, <tp>unpa</tp> also means <eng>to have sex with,</eng> so there's a potential for embarassment and confusion. I haven't seen Toki Ponans actually use <tp>unpa</tp> to mean marriage in real conversations.

<p><lt1>Miscellaneous</lt1>

<p>The miscellaneous word for this lesson is <tp>weka</tp>. As a verb, it just means <eng>to get rid of,</eng> <eng>to remove,</eng> etc.<br>
<ex><tp>o weka e len sina.</tp> <eq></eq> <eng>Take off your clothes.</eng><br>
<ex><tp>o weka e jan lili tan ni. ona li wile ala kute e ni.</tp> <eq></eq> <eng>Remove the kid from here. He shouldn't hear this.</eng>

<p><tp>weka</tp> can also mean <eng>away.</eng><br>
<ex><tp>mi weka.</tp> <eq></eq> <eng>I was away.</eng><br>
<ex><tp>mi wile tawa weka.</tp> <eq></eq> <eng>I want to go away,</eng> or <eng>I want to leave.</eng>

<p><tp>weka tan</tp> <eq></eq> <eng>far from</eng><br>
<ex><tp>tomo mi li weka tan ni.</tp> <eq></eq> <eng>My house is far from here.</eng><br>
<ex><tp>ma Posan li weka tan ma Alensina.</tp> <eq></eq> <eng>Bosnia is far from Argentina.</eng>

<p><tp>weka ala tan</tp> <eq></eq> <eng>close to</eng><br>
<ex><tp>ma Mewika li weka ala tan ma Kupa.</tp> <eq></eq> <eng>The USA is near Cuba.</eng>

