<p><lt1>Vocabulary</lt1>

<p><table class="ldict">
  <tr>
    <td class="dt">pi</td>
     <td class="de">essentially means "of"; see notes below</td></tr>
  <tr>
    <td class="dt">kalama</td>
     <td class="de">sound, noise; to make noise, to play an instrument</td></tr>
  <tr>
    <td class="dt">kulupu</td>
     <td class="de">group, community, society</td></tr>
  <tr>
    <td class="dt">nasin</td>
     <td class="de">road, way, doctrine, method</td></tr></table>

<p><lt1><tp>pi</tp></lt1><p>
Although <tp>pi</tp> basically means <eng>of,</eng> it's one of Toki Pona's more confusing words and probably the most misused. (I've read all sorts of weird errors that students have made while learning how to use <tp>pi</tp>. In a moment we'll look at a funny mistake that occurred when someone didn't use <tp>pi</tp> but should have.) Unfortunately, though, <tp>pi</tp> is a necessary evil. If you get frustrated, just relax, try your best, and know that you have my sympathy.

<p>Remember from <a href="okamasona5.php">lesson 5</a> that <tp>tomo telo</tp> (literally, <eng>water room</eng>) means <eng>restroom.</eng> Also remember that <tp>nasa</tp> means <eng>crazy,</eng> <eng>silly,</eng> <eng>stupid,</eng> etc. With those words in mind, look at this sentence:<br>
<ex><tp>mi tawa tomo telo nasa.</tp> <eq></eq> <eng>I went to the crazy restroom.</eng> (literally, <eng>I to room water crazy.</eng>)<br>
You'll probably agree with me that this is a strange sentence. It makes me think of a creepy restroom with neon lights the floor and a strobe light in every stall. What the student was actually <i>trying</i> to say is that he'd gone to a bar. As you may recall, <tp>telo nasa</tp> <eq></eq> <eng>alcohol.</eng> Therefore, a <tp>tomo</tp> with <tp>telo nasa</tp> would be a bar. However, you can't simply combine <tp>tomo</tp> and <tp>telo nasa</tp> together because it'll mean <eng>crazy restroom</eng> as you just studied. We use <tp>pi</tp> to get around this problem:<br>
<ex><tp>tomo <b>pi</b> telo nasa</tp> <eq></eq> building <b>of</b> alcohol; a bar, pub, etc.<br>
The problem is fixed. Yay!

<p>As you see, <b><u><tp>pi</tp> separates a noun from another noun that has an adjective</b></u>. If you like to think about things in a formulaic, patterned way, here's another way of thinking about it:<br>
<ex>(noun 1) pi (noun 2) (an adjective that modifies noun 2 but NOT noun 1)<br>
There <b><u>must</u></b> be an adjective to describe noun 2. If not, <tp>pi</tp> is not used at all, and you get this:<br>
<ex>(noun 1) (noun 2)<br>
If you're familiar with scheme notation (If not, just skip ahead to the next paragraph.), here's a layout of the example above:<br>
<ex>crazy restroom = ((<tp>tomo telo</tp>) <tp>nasa</tp>)<br>
<ex>bar = (<tp>tomo pi</tp> (<tp>telo nasa</tp>))

<p>Hopefully I explained <tp>pi</tp> well enough that you can understand it. To further your understanding, more examples are below. Several of the examples include compound-noun combinations that you learned earlier in the course, mostly in <a href="okamasona5.php">lesson 5</a>.<br>
<ex><tp>jan pi ma tomo</tp> <eq></eq> person of the city, a city-dweller<br>
<ex><tp>kulupu pi toki pona</tp> <eq></eq> group of Toki Pona, the Toki Pona community<br>
<ex><tp>nasin pi toki pona</tp> <eq></eq> ways of Toki Pona, the ideology behind Toki Pona<br>
<ex><tp>jan lawa pi jan utala</tp> <eq></eq> leader of soldiers, a commander or general<br>
<ex><tp>jan lawa pi tomo tawa kon</tp> <eq></eq> leader of airplanes, i.e. a pilot<br>
<ex><tp>jan pi nasin sewi Kolisu</tp> <eq></eq> person of Christian religion, i.e. a Christian<br>
<xp>Recall: <tp>nasin sewi</tp> = religion</xp><br>
<ex><tp>jan pi pona lukin</tp> <eq></eq> <eng>person of visual goodness,</eng> i.e. an attractive person<br>
<ex><tp>jan pi ike lukin</tp> <eq></eq> <eng>person of visual badness,</eng> i.e. an ugly person<br>
If you don't understand these examples, especially the first five, you should go back and review earlier lessons or find someone who can help you.

<p><lt2>Possessives with <tp>pi</tp></lt2>

<p>You learned in <a href="okamasona5.php">lesson 5</a> that <eng>my house</eng> <eq></eq> <tp>tomo mi</tp>. Likewise, <eng>your house</eng> <eq></eq> <tp>tomo sina</tp>. However, to name a specific person who owns the house, you must use <tp>pi</tp>:<br>
<ex><tp>tomo pi jan Lisa</tp> <eq></eq> house of Lisa, Lisa's house<br>
Note that you can <i>not</i> say <tp>tomo Lisa</tp>, as that would mean something different. Here are more examples:<br>
<ex><tp>kili mi</tp> <eq></eq> <eng>my fruit</eng><br>
<ex><tp>kili pi jan Susan</tp> <eq></eq> <eng>Susan's fruit</eng><br>
<ex><tp>ma ona</tp> <eq></eq> <eng>her country</eng><br>
<ex><tp>ma pi jan Keli</tp> <eq></eq> <eng>Keli's country</eng><br>
<ex><tp>len jan</tp> <eq></eq> <eng>somebody's clothes</eng><br>
<ex><tp>len pi jan Lisa</tp> <eq></eq> <eng>Lisa's clothes</eng>

<p>You also use <tp>pi</tp> for plural possessive pronouns (e.g. <eng>our</eng> and <eng>their</eng>):<br>
<ex><tp>nimi pi mi mute</tp> <eq></eq> <eng>our names</eng><br>
<ex><tp>tomo pi ona mute</tp> <eq></eq> <eng>their house</eng><br>
Without <tp>pi</tp>, these phrases simply would not make sense.

<p><lt2>Opposites with <tp>pi</tp> and <tp>ala</tp></lt2>

<p>With its small vocabulary, Toki Pona sometimes lacks the opposites of certain words. For example, there's a word for noise (<tp>kalama</tp>) but no word for silence. We have a word for strength (<tp>wawa</tp>) but not for weakness.

<p>To get around this problem, use <tp>pi</tp> and <tp>ala</tp> to express the opposite of a word. An example:<br>
<ex><tp>jan wawa</tp> <eq></eq> <eng>strong person</eng><br>
<ex><tp>jan pi wawa ala</tp> <eq></eq> <eng>"person of no strength,</eng> i.e. a weak person<br>
You couldn't say <tp>jan wawa ala</tp> because that phrase would mean <eng>no strong people.</eng>

<p><lt2>Common mistakes</lt2>

<p>One common mistake is using <tp>pi</tp> to mean <eng>about</eng> (as in the sentence, <eng>We talked about something.</eng>). Although <tp>pi</tp> is sometimes used this way, many people over-use it. Here is a common but incorrect way of using <tp>pi</tp> to mean <eng>about</eng> in a sentence, followed by the correct way:<br>
<ex><tp>Wrong: mi toki pi jan.</tp> <eq></eq> <eng>I talked about people.</eng><br>
<ex><tp>Right: mi toki jan.</tp> <eq></eq> <eng>I talked about people.</eng><br>
<eng>about</eng> is simply implied by the sentence. Compare the above sentence with the next one, which correctly uses <tp>pi</tp> to mean <eng>about:</eng><br>
<ex><tp>mi toki pi jan ike.</tp> <eq></eq> <eng>I talked about bad people.</eng><br>
You can use <tp>pi</tp> here is because <tp>jan ike</tp> is its own individual concept, and it acts on <tp>toki</tp> as one thing. <tp>pi</tp> simply distinguishes the <tp>jan ike</tp> phrase.

<p>Another common mistake is simply recklessly over-using <tp>pi</tp>. For example, we already know that <tp>jan lawa</tp> <eq></eq> "leader," but some students carelessly say <tp>jan pi lawa</tp>. Similarly, <eng>countryman</eng> = <tp>jan ma</tp>, but some students will say <tp>jan pi ma</tp>. I don't understand why students make this error in their minds, but regardless,  <b><u>these phrases are wrong because the second noun does not have an adjective to modify it</u></b>. Don't forget the formula:<br>
<ex>(noun 1) pi (noun 2) (an adjective that modifies noun 2, but NOT noun 1)</u></b></ex><br>
<b><u>The word immediately following <tp>pi</tp> must have another word that describes it.</u></b><p>

<p>A third mistake is using <tp>pi</tp> when you should have used <tp>tan</tp> instead:<br>
<ex>Right: <tp>mi kama tan ma Mewika.</tp> <eq></eq> <eng>I come from America.</eng><br>
<ex>Wrong: <tp>mi kama pi ma Mewika.

<p><lt2>Another use</lt2><p>
<tp>pi</tp> has one other use. I'm describing it in a separate section because it somewhat breaks the rules that you learned above. Consider the following sentences:<br>
 <ex><tp>kili ni li pi mi.</tp> <eq></eq> <eng>This fruit is mine.</eng><br>
 <ex><tp>tomo ni li pi jan Tami.</tp> <eq></eq> <eng>That house is Tommy's.</eng><br>
Although it may look a little odd, a <tp>pi</tp> phrase can be used after <tp>li</tp> to tell who owns something. Here are some more examples if you need to look at them:<br>
 <ex><tp>ilo ni li pi sina.</tp> <eq></eq> <eng>This tool is yours.</eng><br>
 <ex><tp>ma ni li pi jan Tosi.</tp> <eq></eq> <eng>This land is the Germans'.</eng><br>
 <ex><tp>toki ni li pi mi mute.</tp> <eq></eq> <eng>This language is ours.</eng>

<p><lt1>Miscellaneous</lt1>

<p>Since learning <tp>pi</tp> is hard enough already, we'll just look at one miscellaneous word in this lesson--<tp>kalama</tp>--and also cover one other use of <tp>nasin</tp>.

<p><lt2><tp>kalama</tp></lt2>

<p><tp>kalama</tp> means "sound" or "noise":<br>
<ex><tp>kalama ni li seme?</tp> <eq></eq> <eng>What was that noise?</eng>

<p>Combine <tp>kalama</tp> with <tp>musi</tp> to mean <eng>music</eng> or <eng>song.</eng><br>
<ex><tp>kalama musi li pona tawa mi.</tp> <eq></eq> <eng>I like music.</eng><br>
Just as <tp>jan</tp> precedes people's names, <tp>kalama musi</tp> precedes the names of specific songs:<br>
<ex><tp>kalama musi "Jingle Bells" li pona tawa mi.</tp> <eq></eq> <eng>I like the song 'Jingle Bells.'</eng><br>
You can also use <tp>pi</tp> to discuss music by a certain group or artist:<br>
<ex><tp>kalama musi pi jan Elton John li nasa.</tp> <eq></eq> <eng>The music of Elton John is odd.</eng><p>

Finally, <tp>kalama</tp> can be a verb:<br>
<ex><tp>mi kalama kepeken ilo.</tp> <eq></eq> <eng>I make noise using an instrument.</eng><br>
<ex><tp>o kalama ala!</tp> <eq></eq> <eng>Don't make noise!</eng><p>

<lt2>Using <tp>nasin</tp> to make "how"</lt2>

<p>Since we covered <tp>nasin</tp> in this lesson, let's look at another important way it's used. Look at this sentence:<br>
<ex><tp>sina pali e ni kepeken nasin seme?</tp> <eq></eq> <eng>You made this using what method? How did you make this?</eng><br>
I think this usage is pretty straight-forward, so I won't provide other examples. Notice, though, that <tp>seme</tp> has been used for all the question words you've learned so far: In the <a href="okamasona10.php">previous lesson</a>, you learned <tp>jan seme</tp> (<eng>who</eng>) and <tp>tan seme</tp> (<eng>why</eng>), and now you've learned <tp>kepeken nasin seme</tp> (<eng>how</eng>)

