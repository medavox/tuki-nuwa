<p><lt1>Adjectives and compound nouns</lt1>
<p>As you should already know, Toki Pona has a very minimal vocabulary. The small amount of words, of course, makes the vocabulary much easier to learn. However, as a result, many words do not exist in the language. For example, there is no word that means <tp>friend</tp>. There are also no words for <tp>soldier</tp>, <tp>car</tp>, or <tp>shoe</tp>. Therefore, we often have to combine various words together to equal what might take only one word in English. For example, here's how to say <tp>friend</tp> in Toki Pona:<br>
<ex><tp>jan</tp> <imp>(person)</imp> <b>+</b> <tp>pona</tp> <imp>(good)</imp> = <tp>jan pona</tp><br>
<tp>jan pona</tp> in English literally means <eng>good person,</eng> but due to Toki Pona's small vocabulary, it also means <tp>friend</tp>.

<p>Notice that the adjective (<tp>pona</tp> in the above example) comes <b>after</b> the noun rather than before it, hence <tp>jan pona</tp> instead of <tp>pona jan</tp>. Although putting an adjective after a noun may seem awkward if you're a native English speaker, many other languages do this. Study until you feel comfortable with this concept.

<p>In addition to adjectives like <tp>pona</tp>, many verbs also have a dual role as adjectives.<br>
<ex>1. <tp>jan</tp> <eq></eq> <eng>person</eng><br>
<ex>&nbsp;&nbsp;&nbsp;&nbsp;<tp>pakala</tp> <eq></eq> <eng>to hurt</eng><br>
<ex>&nbsp;&nbsp;&nbsp;&nbsp;<tp>jan pakala</tp> <eq></eq> <eng>an injured person, victim, etc.</eng><br>
<ex>2. <tp>ilo</tp> <eq></eq> <eng>tool</eng><br>
<ex>&nbsp;&nbsp;&nbsp;&nbsp;<tp>moku</tp> <eq></eq> <eng>to eat</eng><br>
<ex>&nbsp;&nbsp;&nbsp;&nbsp;<tp>ilo moku</tp> <eq></eq> an eating utensil, e.g. a fork or spoon

<p>You can add multiple adjectives to a noun:<br>
<ex><tp>jan</tp> <eq></eq> <eng>person</eng><br>
<ex><tp>jan utala</tp> <eq></eq> <eng>soldier</eng><br>
<ex><tp>jan utala pona</tp> <eq></eq> <eng>good soldier</eng><br>
<ex><tp>jan utala pona mute</tp> <eq></eq> <eng>many good soldiers</eng><br>
<ex><tp>jan utala pona ni</tp> <eq></eq> <eng>this good soldier</eng><br>
Notice that <tp>ni</tp> and <tp>mute</tp> come at the end of the phrase. The phrases build as you go along, so the adjectives must be organized logically. Notice the difference between these two phrases:<br>
<ex><tp>jan utala pona</tp> <eq></eq> <eng>good soldier</eng><br>
<ex><tp>jan pona utala</tp> <eq></eq> <eng>fighting friend,</eng> i.e. a sidekick, etc.

<p>Here are some handy, commonly used phrases using words that you've already learned. Try to figure out their literal meanings:<br>
<ex><tp>pona lukin</tp>* <eq></eq> <eng>pretty,</eng> <eng>good-looking,</eng> etc.<br>
<ex><tp>ike lukin</tp>* <eq></eq> <eng>ugly</eng><br>
<ex><tp>jan ike</tp> <eq></eq> <eng>enemy</eng><br>
<ex><tp>jan lawa</tp> <eq></eq> <eng>leader</eng><br>
<ex><tp>jan lili</tp> <eq></eq> <eng>child</eng><br>
<ex><tp>jan sewi</tp> <eq></eq> <eng>god</eng><br>
<ex><tp>jan suli</tp> <eq></eq> <eng>adult</eng><br>
<ex><tp>jan unpa</tp> <eq></eq> <eng>lover</eng><br>
<ex><tp>ma telo</tp> <eq></eq> <eng>mud</eng> or <eng>swamp</eng><br>
<ex><tp>ma tomo</tp> <eq></eq> <eng>city</eng> or <eng>town</eng><br>
<ex><tp>mi mute</tp> <eq></eq> <eng>we,</eng> <eng>us</eng><br>
<ex><tp>ona mute</tp> <eq></eq> <eng>they,</eng> <eng>them</eng><br>
<ex><tp>telo nasa</tp> <eq></eq> <eng>alcohol,</eng> <eng>beer,</eng> <eng>wine</eng><br>
<ex><tp>tomo telo</tp> <eq></eq> <eng>restroom</eng>

<p>* <small>Note that you can only use <tp>pona lukin</tp> and <tp>ike lukin</tp> by themselves after <tp>li</tp>. (For example: <tp>jan ni li pona lukin</tp> <eq></eq> <eng>That person is pretty.</eng>) There is a way to attach these phrases directly onto the noun using the word <tp>pi</tp>, but you won't learn about that until <a href="okamasona11.php">lesson 11</a>.</small>

<p><lt1>Possessives</lt1><p>
To say <tp>my</tp> and <tp>your</tp>, use <tp>mi</tp> and <tp>sina</tp> like like any other adjective:<br>
<ex><tp>tomo mi</tp> <eq></eq> <eng>my house</eng><br>
<ex><tp>ma sina</tp> <eq></eq> <eng>your country</eng><br>
<ex><tp>telo ona</tp> <eq></eq> <eng>his/her/its water</eng><br>
Other words are treated the same way:<br>
<ex><tp>len jan</tp> <eq></eq> <eng>somebody's clothes</eng><br>
<ex><tp>seli suno</tp> <eq></eq> <eng>the sun's heat</eng>

<p><lt1>Adverbs</lt1>

<p>Toki Pona's adverbs simply follow the verbs they modify. A few examples:<br>
<ex><tp>mi lawa <u>pona</u> e jan.</tp> <eq></eq> <eng>I lead people <u>well</u>.</eng><br>
<ex><tp>mi utala <u>ike</u>.</tp> <eq></eq> <eng>I fight <u>badly</u>.</eng><br>
<ex><tp>sina lukin <u>sewi</u> e suno.</tp> <eq></eq> <eng>You look <u>up</u> at the sun.</eng><br>
<ex><tp>ona li wile <u>mute</u> e ni.</tp> <eq></eq> <eng>He wants that <u>a lot</u>.</eng><br>
<ex><tp>mi mute li lukin <u>lili</u> e ona.</tp> <eq></eq> <eng>We <u>barely</u> saw it.</eng><br>

