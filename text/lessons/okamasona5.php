<p>&iexcl;Ay caramba! These last two lessons, and especially this one, may have seemed really hard. You'll have to start trying to think for yourself rather than relying on English first. If you haven't quite got it yet, it's okay; these things have to be learned by practice and association, so you'll get it in time. If you still feel disheartened, think about this: If you've been learning and practicing the vocabulary of these past three sections like you should, you've already learned over a quarter of all the words in Toki Pona! Keep up the good work!

