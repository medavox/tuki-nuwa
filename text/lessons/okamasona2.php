<p>As you may have noticed, Toki Pona's <tp>j</tp> isn't pronounced like English's <tp>j</tp>. Instead, always pronounce it like the letter <i>y</i> in English. Some languages (e.g., German and Esperanto) also pronounce <tp>j</tp> this way, but if you've never studied such languages, be careful to pronounce it right. If you see <tp>j</tp>, pretend it's <i>y</i>.

<p><lt1>Vowels</lt1>

<p>In English, vowels are pronounced many different ways depending on the word. In Toki Pona, though, the vowels are all regular and never change pronunciation.

<p>If you're familiar with Spanish, Italian, or Esperanto, you'll recognize that Toki Pona's vowels are the same as in those languages. Study this chart:

