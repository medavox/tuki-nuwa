<p><lt1>More Advanced Stuff</lt1>

<p>As long as you've mastered the above material, you know everything you need to continue learning basic Toki Pona. If learning the alphabet and pronunciation was easy for you and if you feel confident, go ahead and read the rest of this lesson now. However, if you had a difficult time with the above material, just focus on it for now, and return to finish this lesson at a later time. (The information is still important if you hope to master Toki Pona, so come back to it later.)

<p>There are a few more things to learn about Toki Pona pronunciation and style:

<p>Official Toki Pona words (i.e., the <a href="dictionary.php">120 words</a> published in Sonja Lang's book, <a href="http://www.amazon.com/gp/product/0978292308"><i>Toki Pona: The Language of Good</i></a>) are never capitalized. They are lowercase even at the beginning of a sentence!

<p>Capitalize unofficial words (i.e., the names of people, places, religions, etc.) A few examples:<br>
<ex><tp>jan Susan li pona.</tp> = <eng>Susan is good.</eng> (Susan, a person's name, is the only word capitalized in this sentence.)<br>
<ex><tp>ma Elopa li suli.</tp> = <eng>Europe is big.</eng> (<eng>Europe</eng> isn't one of Toki Pona's 120 official words, so it's capitalized.)

<p>Because Toki Pona has so few consonants, you have some flexibility with pronouncing them. In addition to the pronunciations you learned above, here's a quick list of some of the alternate pronunciations that certain consonants can have:

<p><table class="ldictc">
  <tr>
    <td class="header">letter</td>
    <td class="header">pronounced like in</td></tr>
  <tr>
    <td class="dt">p</td>
    <td class="de"><b>b</b>it</td></tr>
  <tr>
    <td class="dt">t</td>
    <td class="de"><b>d</b>og</td></tr>
  <tr>
    <td class="dt">k</td>
    <td class="de"><b>g</b>arden</td></tr>
  <tr>
    <td class="dt">s</td>
    <td class="de"><b>z</b<br>oo</td></tr></table>

<p>Toki Pona's syllables all follow a pattern: consonant + vowel + optional <tp>n</tp>. If a syllable is at the beginning of a word, the consonant is optional. (For example, <tp>ali</tp> and <tp>unpa</tp> don't begin with consonants.) The optional <tp>n</tp> is forbidden if the next syllable begins with <tp>m</tp> or <tp>n</tp>. Four combinations are forbidden due to difficulty hearing and/or saying them; these are: <tp>ji</tp>, <tp>ti</tp>, <tp>wo</tp>, and <tp>wu</tp>.

<p>Accent is always on the first syllable. 

