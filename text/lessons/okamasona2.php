<p>There are fourteen letters in the Toki Pona alphabet: nine consonants (j k l m n p s t w) and five vowels (a e i o u).

<p><lt1>Consonants</lt1>

<p>Except for <tp>j</tp>, all the consonants are pronounced like in English.

