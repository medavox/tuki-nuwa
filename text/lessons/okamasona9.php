<p><lt1>Gender</lt1>

<p>Like English (but unlike other Western languages), Toki Pona doesn't use grammatical gender, which makes the language much easier to learn and use. However, sometimes you may want to specify which gender a noun is, and you can use <tp>mije</tp> and <tp>meli</tp> to do so. For example:<br>
<ex><tp>mama</tp> <eq></eq> a parent in general; doesn't distinguish mother versus father<br>
<ex><ex><tp>mama meli</tp> <eq></eq> <eng>mother</eng><br>
<ex><ex><tp>mama mije</tp> <eq></eq> <eng>father</eng><br>
It's as simple as that. Keep in mind, though, that specifying gender is often unnecessary. Don't specify unless you have a special reason. 

<p><lt1>Unofficial Words</lt1>

<p>If you browse through the <a href="dictionary.php">Toki Pona dictionary</a>, you'll see many words you've already learned, but you won't see any words for specific countries, nor for religions, nor even for other languages. Such words aren't in the dictionary because they're <b>unofficial</b>. Anything other than the 124 words in the dictionary is unofficial.

<p>Before using an unofficial word, Toki Ponans often adapt the word to obey Toki Pona's phonetic rules. For example, America becomes <tp>Mewika</tp>, and Canada becomes <tp>Kanata</tp>. Links to lists of other unofficial words are on the <a href="learn.php">"learn" page</a>. There's also a <a href="tpize.php">guide for phonetically adapting words</a>, but it requires considerable linguistics knowledge.

<p><lt2>Countries, Cities, Languages</lt2>

<p>Unofficial words can never stand alone as nouns. They always act as adjectives and therefore must be used with a noun. Let's suppose, for example, that you want to say, <eng>Canada is good.</eng> Because <tp>Kanata</tp> is an unofficial word, it has to be used with a noun, and since we're talking about the <b>country</b> of Canada, we should use the noun <tp>ma</tp> (<eng>land,</eng> <eng>country,</eng> etc.). Therefore, <eng>Canada is good</eng> <eq></eq> <tp>ma Kanata li pona.</tp> Unofficial words are treated the same as all other adjectives in Toki Pona, except they're capitalized to help distinguish them from the unofficial words. Here are some more sentences using the names of countries:<br>
<ex><tp>ma Wensa li nasa.</tp> <eq></eq> <eng>Sweden is crazy.</eng><br>
<ex><tp>ma Lowasi li pona lukin.</tp> <eq></eq> <eng>Croatia is pretty.</eng><br>
<xp>Remember from lesson five that <tp>pona lukin</tp> means <eng>pretty,</eng> <eng>attractive,</eng> etc. It literally means <eng>good visually.</eng>)</xp><br>
<ex><tp>mi wile tawa ma Netelan.</tp> <eq></eq> <eng>I want to go to the Netherlands.</eng>

<p>You may remember from <a href="okamasona5.php">lesson 5</a> that <tp>ma tomo</tp> <eq></eq> <eng>city.</eng> To mention a city in Toki Pona, say <tp>ma tomo</tp> (instead of just <tp>ma</tp>) then the name of the city.<br>
<ex><tp>ma tomo Lantan li suli.</tp> <eq></eq> <eng>London is big.</eng><br>
Once again, the unofficial word (<tp>Lantan</tp>) is used as an adjective. Here are a few other major cities:<br>
<ex><tp>ma tomo Pelin</tp> <eq></eq> <eng>Berlin</eng><br>
<ex><tp>ma tomo Elena</tp> <eq></eq> <eng>Atlanta</eng><br>
<ex><tp>ma tomo Loma</tp> <eq></eq> <eng>Rome</eng>

<p>To recap, use <tp>ma</tp> before a country's name, and <tp>ma tomo</tp> before a city's name. For a language's name, use <tp>toki</tp>, and to describe a person who's from a certain place, use <tp>jan</tp>. The unofficial word doesn't change.<br>
<ex><tp>ma Inli li pona.</tp> <eq></eq> <eng>England is good.</eng><br>
<ex><tp>toki Inli li pona.</tp> <eq></eq> <eng>The English language is good.</eng><br>
<ex><tp>jan Inli li pona.</tp> <eq></eq> <eng>Englishmen are good.</eng><br>
If you have a good reason to specify gender, you can change <tp>jan</tp> to either <tp>mije</tp> or <tp>meli</tp>.<br>
<ex><tp>meli Italija</tp> <eq></eq> <eng>Italian woman</eng><br>
<ex><tp>mije Epanja</tp> <eq></eq> <eng>Spanish man</eng>

<p><lt2>Personal Names</lt2>

<p>To talk about someone using his or her name, use <tp>jan</tp> then the person's name:<br>
<ex><tp>jan Lewi li utala e jan Ten.</tp> <eq></eq> <eng>Larry attacked Dan.</eng><br>

<p>As you can see, we Toki Ponans often phonetically modify a person, just as you saw above for country names. Here are more example sentences using "TokiPonized" names:<br>
<ex><tp>jan Pentan li pana e sona tawa mi.</tp> <eq></eq> <eng>Brandon teaches me.</eng><br>
<xp><tp>pana e sona</tp> literally means <eng>to give knowledge.</eng> It's used to mean <eng>to teach.</eng></xp><br>
<ex><tp>jan Mewi li toki tawa mi.</tp> <eq></eq> <eng>Mary's talking to me.</eng><br>
<ex><tp>jan Nesan li musi.</tp> <eq></eq> <eng>Nathan is funny.</eng>

<p>There are two ways to introduce yourself:<br>
<ex><tp>mi jan Pita.</tp> <eq></eq> <eng>I am Peter.</eng><br>
<ex><tp>nimi mi li Pita.</tp> <eq></eq> <eng>My name is Peter.</eng><br>

<p><lt1>Addressing People, Commands, Interjections</u></b></font><p>

<lt2>Addressing People</lt2>

<p>Sometimes you may need to call out someone's name in order to get his attention, especially before you continue speaking. Here's how to do that in Toki Pona:<br>
<ex><tp><b>jan</tp> (name) <tp>o</tp> &#177; (rest of sentence).</b><br>
Here are some examples:<br>
<ex><tp>jan Luta o!</tp> <eq></eq> <eng>Luther!</eng><br>
<ex><tp>jan Ken o, pipi li lon len sina.</tp> <eq></eq> <eng>Ken, there's a bug on your shirt.</eng><br>
<ex><tp>jan Keli o, sina pona lukin.</tp> <eq></eq> <eng>Kelly, you are pretty.</eng><br>
<ex><tp>jan Mawen o, sina wile ala wile moku?</tp> <eq></eq> <eng>Marvin, are you hungry?</eng><br>
<ex><tp>jan Tepani o, sina ike tawa mi.</tp> <eq></eq> <eng>Steffany, I don't like you.</eng><br>
Try to remember to put the comma after the <tp>o</tp>. (Otherwise your statement might be mistaken for a command, which you'll soon learn about.)

<p>To add emotional emphasis, you may add <tp>a</tp>* after the <tp>o</tp>. An example:<br>
<ex><tp>jan Epi o a!</tp> <eq></eq> <eng>Oh, Abbie!</eng><br>
Only use <tp>a</tp> for particularly emotional greetings/events (e.g., if you haven't seen the person in a long time, or during sex). Also notice that it's not necessary to write a sentence after you've gotten the person's attention.

<p><small>* The <a href="dictionary.php">official dictionary</a> lists <tp>a</tp> and <tp>kin</tp>--a word you'll learn in <a href="okamasona12.php">lesson 12</a>--as being interchangeable. Traditionally, though, these two words had different connotations and uses, and most Toki Ponans seem to use these two words according to their traditional definitions, so this course will teach them that way.</small>

<p><lt2>Commands</lt2>
<p>To state a command in Toki Pona, just say <tp>o</tp> and then whatever you want the person to do.<br>
<ex><tp>o pali!</tp> <eq></eq> <eng>Work!</eng><br>
<ex><tp>o awen.</tp> <eq></eq> <eng>Wait.</eng><br>
<ex><tp>o lukin e ni.</tp> <eq></eq> <eng>Watch this.</eng><br>
<ex><tp>o tawa ma tomo lon poka mi.</tp> <eq></eq> <eng>Go to the city with me.</eng>

<p>Now that you've learned how to get someone's attention and make commands, let's learn how to do both in the same sentence:<br>
<ex><eng>John, go to your house.</eng><br>
<ex><ex><font color=ff7777>&rarr;</font> <tp>jan San o</tp> <imp>(<eng>John,</eng>)</imp> <font color=ff7777>+</font> <tp>o tawa tomo sina</tp> <imp>(<eng>Go to your house.</eng>)</imp> <br>
<ex><ex><font color=ff7777>&rarr;</font> <tp>jan San o tawa tomo sina.</tp><br>
Notice you have to drop one of the <tp>o</tp>'s and the comma, too. Here are some more examples:<br>
<ex><tp>jan Ta o toki ala tawa mi.</tp> <eq></eq> <eng>Todd, don't talk to me.</eng><br>
<ex><tp>jan Sesi o moku e kili ni.</tp> <eq></eq> <eng>Jessie, eat this fruit.</eng><br>
You also use this structure to say things like, <eng>Let's go.</eng><br>
<ex><tp>mi mute o tawa.</tp> <eq></eq> <eng>Let's go.</eng><br>
<xp>Remember from <a href="okamasona5.php">lesson 5</a> that <tp>mi mute</tp> <eq></eq> <eng>we.</eng></xp><br>
<ex><tp>mi mute o musi.</tp> <eq></eq> <eng>Let's have fun.</eng>

<p><lt2>Interjections</lt2>

<p>There are basically two types of interjections: simple, one-word interjections; and friendly, salutation-like phrases.

<p>One-word interjections:<br>
<ex><tp><u>toki</u></tp> is used to mean hello.<br>
<ex><tp>&nbsp;&nbsp;toki!</tp> <eq></eq> Hello!, Hi!, etc.<br>
<ex><tp>&nbsp;&nbsp;jan Lisa o, toki.</tp> <eq></eq> Hello, Lisa.<br>
<ex><tp><u>pona</u></tp> is what you say when something good happens.<br>
<ex><tp>&nbsp;&nbsp;pona!</tp> <eq></eq> Yay!, Good!, Hoorah!<br>
<ex><tp><u>ike</u></tp> is what you say when something bad happens.<br>
<ex><tp>&nbsp;&nbsp;ike!</tp> <eq></eq> Oh no!, Uh oh!, Alas!, etc.<br>
<ex><tp><u>pakala</u></tp> essentially covers all the curse words.<br>
<ex><tp>&nbsp;&nbsp;pakala!</tp> <eq></eq> f-ck! d-mn!<br>
<ex><tp><u>mu</u></tp> is for sounds made by animals.<br>
<ex><tp>&nbsp;&nbsp;mu.</tp> <eq></eq> Meow. Woof. Grrr. Moo.<br>
<ex><tp><u>a</u></tp> is a word that expresses emotion or laughter.<br>
<ex><tp>&nbsp;&nbsp;a.</tp> <eq></eq> Ooh, ahh, unh, oh, etc.<br>
<ex><tp>&nbsp;&nbsp;a a a!</tp> <eq></eq> Hahaha! [laughter]

<p>Salutation-like phrases:<br>
<ex><tp>suno pona!</tp> <eq></eq> Good sun! Good day!<br>
<ex><tp>lape pona!</tp> <eq></eq> Sleep well! Have a good night!<br>
<ex><tp>moku pona!</tp> <eq></eq> Good food! Enjoy your meal!<br>
<ex><tp>mi tawa</tp> <eq></eq> I'm going. Bye!<br>
<ex><tp>tawa pona!</tp> <eq></eq> (in reply) Go well! Good bye!<br>
<ex><tp>kama pona!</tp> <eq></eq> Come well! Welcome!<br>
<ex><tp>musi pona!</tp> <eq></eq> Good fun! Have fun!<br>
Don't use <tp>o</tp>. <tp>o</tp> is only for commands, not for friendly greetings.

